<h2>Saturday</h2>

<h3>Swildon's: Rhys, Oli, Sam, Fiona Dave KP, Saber, Guilio, William</h3>

<p>After 5 weeks of caving expedition we were all feeling like toughened alpine cavers. Of course this is not the face one wants to present to fresher’s. What if they were scared off by our extreme technical ability and absolute faultlessness? So a delightful weekend in the countryside was called for to reacquaint ourselves with the faff and drunken debauchery of uk caving. The SMCC hut in the Mendips was selected without hesitation.</p>

<p>Saturday saw everyone up super early (must’ve been like 10am!) because I had single handedly prepared breakfast and was keen for people to eat and go caving. I was reminded during breakfast that we are constitutionally forbidden from going caving before midday so we chilled out, maxed and relaxed all cool till 12. Once sun passed its peak, we donned neoprene and PVC protection and set out towards Swildons, for a quick short round trip.</p>

<p>We took the scenic route to the cave and were inside in less than an hour. I expertly led us through to the ladder pitch. We lost Saber briefly but he appeared in front of us. I can only assume he has mastered teleportation. At the pitch we were pleased to find that the water level was very low and that this time climbing the ladder was less akin to swimming. Guilio demonstrated Italian (or Munter’s) hitches to everyone and we all abbed/climbed down.</p>

<p>This is where my expert knowledge ran out. On the last trip (6 months ago) I had escorted Janet out from this point and not returned. My other trips here were as a fresher with Tetley and so I had more to worry about than remembering the route. We continued dauntlessly, guided by half remembered descriptions of the cave from our 1970’s guide book (our modern one had the Swildons pages ripped out; sabotage perhaps).</p>

<p>After a short while we unexpectantly arrived at sump 1. We had intended to go to Tratman’s Temple and complete the short round the normal way but it would later transpire that we had actually climbed into Barne’s Loop and just dropped back into the streamway. It had looked surprisingly pretty. The sump wasn’t actually sumped and we decided that the water level was low enough that we could do the short round backwards.</p>

<p>Oli, unsportingly, attempted the Sump as a duck and got a noseful of water as punishment. Everyone else pretended it was sumped, in order to do the trip properly. We were soon away down the main streamway and up the slippery handlined climb. The ducks were all low and we were able to navigate them. Guilio, demonstrating the indefatigable Italian spirit, did them as sumps face down.</p>

<p>We were all relieved to see Birthday squeeze, it being proof of the quality our navigational skills. Each of us slithered through with little problem. Beyond lay territory none of us were overly familiar with, due to high water levels preventing exploration of this area on the last trip. We soon came to a multiple of ways on and spent the better part of an hour plumbing the depths of each nook and cranny. After an exhaustive search we were halfway to our callout and so we decided to head back the way we came. Everyone was eager to see the ducks and sump again anyway.</p>

<p>A party of fast and overly cautious cavers was sent ahead to ensure our callout was not missed. It turned out not to be necessary though. Even though everyone took the scenic route back to the hut we were all in before callout.</p>

<p>We whiled away the evening hours drinking, chatting and (for the most dedicated) vomiting.</p>

