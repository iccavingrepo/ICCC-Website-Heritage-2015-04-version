Mud-monster Ben slithered &amp; slurped into his festering [<i>Ben says 'It was the freshest of mud - only three days old!</i>] and horrendously heavy caving gear; fresh from a dig in Rose-Cottage Cavern. Sandeep 'Spatial Awareness' Mavadia had picked out a too-small oversuit for Michelle, which I, being a skinny runt, could just about squeeze into; though with the constant fear of being bisected from the groin up if I bent over too quickly...Bimbled through the plush scenary towards the cave entrance; meeting a party of three exiting cavers with sparklingly clean kit. Not that unusual for an ICCC trip to meet exiting cavers as we go in, but it was a mere One O'Clock today.</p>

<p>Ben wrestled the padlocked entrance open as the rest of us admired the babbling brock and small stream which seamed to be diving down throug the cracks in the concrete... T'was going to be a wet one!</p>

<p>
<a href="/caving/photo_archive/trips/2005-02-25%20-%20mendips/longwood/IMG_0378.html">
<img align=left src="/caving/photo_archive/trips/2005-02-25%20-%20mendips/longwood/IMG_0378--thumb.jpg">
</a>
<a href="/caving/photo_archive/trips/2005-02-25%20-%20mendips/longwood/IMG_0384.html">
<img align=right src="/caving/photo_archive/trips/2005-02-25%20-%20mendips/longwood/IMG_0384--thumb.jpg">
</a>

Descent quickly tightened into an extremely drippy and fairly tight chimney down a rift, continuously snatching at my helmet as I clambered. Lifting my knees to climb on the way up was going to be interesting! Vertical stuff bottomed out and changed into a fairly wretched flat-out crawl through rift, generally out of the stream. One particular bit however involved creative reverse parking around a right-angle bend with my chest dragged fully through the water. Was most glad for the 5mm wetsuit! Jo being Jo, managed to crawl straight around without even so much as dipping her chin in the water!</p>

<p>
Rift meandered down pleasently; briefly opening up fully for Showerbath chamber, involving a dash through a pretty full flowing waterful and then a dive down into rift. Again pretty tight; but with gravity assistence we were making extremely good time... First ladder [supposedly free-climable] pitch was really quite pleasent, nice rigging off a wire grooved calcite column and a gentle slope onto the vertical section. Passageway continued fairly openly until reaching another waterfall; where a brief snooze was enjoyed as the 'boys rigged a dodgy traverse with our Old dynamic rope and a penknife. Rigging was shonky; rope tied securely round trapped boulder on the nearside, but then stuck to progressively backed up pieces of mud on the other... Ideal place for a few P-Bolts?</p>

<p>Traverse was fine for the long-legged, one can cuddle the overhang rock while placing the outreaching leg on the rock shelf beyond. Would be pretty dodge without a handline for the sub-six-footers.</p>

<p>
<a href="/caving/photo_archive/trips/2005-02-25%20-%20mendips/longwood/IMG_0385.html">
<img align=left src="/caving/photo_archive/trips/2005-02-25%20-%20mendips/longwood/IMG_0385--thumb.jpg">
</a>
<a href="/caving/photo_archive/trips/2005-02-25%20-%20mendips/longwood/IMG_0399.html">                                                         <img align=right src="/caving/photo_archive/trips/2005-02-25%20-%20mendips/longwood/IMG_0399--thumb.jpg">
</a>

Clambered down into an impressively large chamber; vertical waterfall transitioning smoothly into 45-degree slope, one would go an awful long way if you slipped on the traverse! Popped back up for the tackle sack, then followed the way on along a rock-shelf round to the right, finally diving down a 45 degree tube into a little chamber with loud crashing water. Second ladder pitch, accessed by climbing over the pitch and then rigging from the otherside, had a veritable Torrent of water exiting horziontally across the pitch with much gusto. Considering the weight of the water crashing down directly where the ladder would have to be rigged, a general decision was made to exit slowly, exploring other reaches.</p>

<p>Back in the main chamber, meandering crawly rift way was followed down to its plughole [which Ben dipped himself in] ending, we had followed the general run of the way on - and must have been physically very close to the head of the 2nd ladder pitch. Rift was pushed upstream from a junction to a grim and low-ceiling'd entrance. Managed to smash my knee into a projecting rock flake, managing to get that sweet-spot without knee-paddage where you can try your best to cleave your knee caps off.</p>

<p>Smooth + Steady exit was then executed; slight mis-navigation just before the Showerbath where GI-Jo + I pushed up the streamway to an increasingly tight and wet squeeze [rock was noticeably rougher just beyond where it got 'too tight']. Backed up and followed the climb up into and through the Waterfall.</p>

<p>
<a href="/caving/photo_archive/trips/2005-02-25%20-%20mendips/longwood/IMG_0404.html">
<img align=left src="/caving/photo_archive/trips/2005-02-25%20-%20mendips/longwood/IMG_0404--thumb.jpg">
