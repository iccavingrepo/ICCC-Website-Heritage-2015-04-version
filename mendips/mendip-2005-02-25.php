Squeezy rift was quite exhausting work to pass, last bit of chimney was particularly grim - couldn't look up into the stinging water, couldn't see beyond knees when looking down - left groping in the dark. Padlock proved troublesome but not horrendous, a bit of jiggling and we were soon outside into the bright daylight! Maud + Denis had a little problem navigating just before the wet right-angled bend, continuing up instead of diving left over flow-stone into the low-ceiling'd passage. Ben + Clew appeared soon after; Clewin having found the chimney pretty dire - tacklebag of wet rope + droplet coated / mud smeared glasses effectively his visibility to nothing.</p> 

<p>
Ben, in his element, dived into the brook to wash the last remains of the mud off his now beautifully clean gear! One wonders whether his choice of cave was somehow motivated by his reluctance to clean his kit...</p>

<p style="text-align:right">
Jarvist Frost
</p>

<h3>Swildons [attempted short round]: Darryl, Sandeep, Adam, Oded</h3>
<p>IT WAS THE BEST OF TIMES, IT WAS THE WORST OF TIMES. STOPPED BY 45 MINUTES OF BAILING MUD SUMP, BACK TO THE WOBBLY LADDER AND WANING DAYLIGHT.</p>
<p style="text-align:right">
Adam Dobson
</p>

<p>Short trip down to Mud Sump. Intended to do the short round, but after bailing for almost an hour, the sump had air space but we had no inclination to carry on. Headed back for food at the Belfry.</p>
<p style="text-align:right">
Darryl Anderson
</p>

<p>After a lot of shaking, pulling and grunting Gerardo was in his wetsuit and we were off, it was only later that I realised it was inside-out, but hey-ho. There was some trouble with oversuits, though I absolve all responsibility of the Tackle Master, how could I have known that an oversuit about my size wouldn.t fit Michelle? Soon we were within Swildons and saying a fond .cya later. to the other group who were embarking upon an exploration of the upper series. 
</p>

<p>We were off and all was well down the entrance and along the climbs, we arrived at the ladder. I went down first as none of the freshers had experienced the swinging joys of electron ladders before. Next down was Adam, he was fine but I think holding the ladder at the bottom helped a bit, Gerardo missed a few foot holds but got down fine in the end and Oded followed like a pro. Daryl ended getting belayed by the group behind; maybe he didn.t trust being belayed from the bottom by me, I wonder why?
</p>

<p>Daryl expertly reminded me about the dwarf traps, twin pots, but I still climbed right into the middle of the first one. They weren.t a scratch on Grenoble but still deep enough; I learnt my lesson for the next one and traversed around the edge of it.
</p>

<p>Everybody climbed above the stream way at Trapman.s temple surprisingly well, quite annoyed that I hadn.t seen a fresher bounce and fall back into the stream way I followed behind. From there on we had a few squeezes, and although Gerardo didn.t seem to enjoy these too much, he carried on like a trooper. A bit more squeezing and climbing and we were at the mud sump where we meet the Reading guys who had passed us at Trapmans Temple.
</p>

<p>This was where Adam-the-mad-mud-sump-bailer got into his stride, he was bailing away at the top dam and keeping up with the other 7 people feeding him with fetid water. The Reading guys were there for 15 minutes already, and it took a further 45 minutes of combined effort before we saw some airspace. In total I think we must have moved a few cubic meters [tons!] of water. By now the freshers were tiring and a commitment to the short-round trip was judged by Darryl to be foolhardy, so after seeing the Reading push through, we decided to abort.
</p>

<p>The way out was a lot faster than going in, partially because of Gerardo.s .I just wanted to get out of that [squeeze]. approach. Thanks to Adams sneakily packed waterproof bag there was plenty of chocolate to go around as well as a drop of refreshing water.
</p>

<p>Descending through Trapmans Temple was more difficult than the ascent, as is so often with freeclimbs. I ended up wedging myself under the particularly fresher unfriendly bits, even though I know that I.m a pretty pathetic as a doorstop, and slowly coaxed them down. 
</p>

<p>At the ladder I belayed Daryl up, this time he had no choice in the matter! He didn.t fall off, so I wasn.t put to the test. The rope was just around a pulley at the top so if he had properly dropped, he.d [not so] slowly fall to the floor and while I.d end up suspended in mid air by my battery belt.
</p>

<p>Everybody headed on out with out much problem, except for a few climbs where you had to think first instead of just launching yourself up and where I walked into a dead-end rift and then looked up to see Daryl grinning and waving while saying .what are you doing down there you muppet?.
</p>

<p>We were out by 6, clothed &amp; in the van by 7. By all accounts an excellent trip even though we didn.t lose any freshers.
</p>

<p style="text-align:right">
Sandeep Mavadia
</p>


