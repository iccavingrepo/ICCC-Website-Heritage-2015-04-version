<h3>Saturday</h3>

<p>
  <b>Swildon's</b> - <i>Short Round, via Sump 4 (5hrs)</i><br>
  Jan, clewin, Darryl, Joachim, Neel, Moritz, Pete
</p>

<p>
  &nbspFollowed the streamway down then climbed upto Tratman's Temple, bailed the Mud Sump very efficiently and until it was almost dry! Pete was not impressed. Continued past Shatter Pot (start of long round) and arrived at start of the Troubles, tried to start the siphon running but it wouldn't work. A rift in the floor was the start of Blue Pencil Passage, a <s>nasty</s> <s>evil</s> <s>tight as f**k</s> LOVELY crawl quickly leading to a <s>contorted</s> <s>horrible</s> JOYOUS right angle bend, which had to be done feet first and then dropped out a 4m climb into Swildons Four.
</p>
<p>
  &nbspHad a look at Sump 3, Sump 4 looked low and horrible, a rope hung down from Cowsh*t Aven (leading out of Priddy Green Sink), we went back the streamway was very nice.
</p>
<p>
  &nbspThere are FOUR <s>squalid</s> <s>muddy</s> WARM ducks in the Trouble Series leading eventually to the awkward Birthday Squeeze and The Landing, an excellent slide back into the main streamway, which is Swildons Two. We followed the large streamway until Sump 1, the sump was only about a foot long long due to low water, but long enough! With the only option to go back through countless horrible ducks we all plunged through without, much, hesistation.
</p>
<p>
  &nbspWe stormed up Swildons One and exited to beautiful sunshine at about 4.30pm.
</p>

