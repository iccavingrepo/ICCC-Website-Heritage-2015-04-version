<h2>Saturday</h2>

<h3>Swildon's short round: Clare, James, Victor and Sam</h3>

<p>I had invited my friend Victor to try caving and was therefore very keen not only that we had an exciting trip, but also that I impress him with my expertise, skill and style as a speleologist. The first skill in the armament of a denizen of the underworld is orientation, based on sound recognition of landmarks and on a steely memory. Clare and I both demonstrated this skill in abundance by getting rather lost on our way to the cave...</p>

<p>No matter I think, at least underground we can demonstrate our skillz. "We should go down the dry way and Jacob's ladder" - I announce to Clare, noting that Victor and Sam are both impressed and feeling reassured by the know-how of their glorious leaders. I take the slot to the right, and soon we are back in the entrance and wading through water. "It is always a good idea to follow water", I say sagely, hoping none realised how undry the "dry way" is turning out to be.</p>

<p>"Bla bla bla geology" I shout, Clare nods in approval and we keep going down the series of waterfalls. My guest realises this is not a simple sport, but also a display of my Renaissance-man knowledge. I hope none notices my falling down the last step of the '40 ft' climb, I was the first down so should have got away with it. By this point it is clear that heat management might be an issue. Victor is so hot he is standing under the waterfall, Sam on the other hand looks a little "chilly" in his fleece garb. "He's young, strong and healthy", I hope to myself.</p>

<p>Soon it is time to look for Tratman's temple. Obviously we completely miss this on our way in, but on the way back luckily find a group of people descending from it. Another testament to our uncanny route finding skills. We follow our noses (or rather, Clare's nose) and eventually get to the Mud Sump. It looks like it has been baled!Awesome! Through we go, Victor loves it and decides to hang out in the water a little longer to cool down... wtf? Sam seems a bit "thrashy", if you catch my drift, I guess he is one of those rare people who does not enjoy small passages mostly filled by icy water. "He's young, strong and healthy", I repeat to myself.</p>

<p>The double troubles are soon reached, and alas they are not baled. Clare, Sam and Victor all sit around while I ineffectually bale the sump by chucking water with a bucket. We have a rather unsuccessful attempt at syphoning by taking the tube up in the upper pool and filling it with water. Eventually we decide the sump has been emptied sufficiently and go for it. I am loving the ducks. Smoothly floating about like like a canoe in water. I wish someone could see it!</p>

<p>At this point we bump into the Jarv-ster and his merry companions. Nico and Kate looked trashed, but the freshers are all bushy tails and bright eyes. I know the next stage involves looking for the blasted rock with the notch. I crawl down behind a boulder, everything looks a bit "bouldery" and not really well travelled. However I can hear the streamway below, so I keep going. It turns out that we are on the opposite side of the ramp, where Tetley and his boys look on aghast as we slide precariously without a rope protection. Gnarly.</p>

<p>The way out is enjoyable, we meet Dan at Sump 1, get caught up by Tetley at the ladder, get out before dark and enjoy a well deserved tinny on our walk back.</p>

<p  style="text-align:right">James Kirkpatrick</p>

