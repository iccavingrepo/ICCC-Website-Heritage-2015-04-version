<h3>Lots of lovely cavers...</h3>

<p>Writing this two whole months after the actual trip, so details might be a little fuzzy. In fact, they may be entirely made up.</p>

<h2>Friday</h2>

<p>The first trip of the spring term, and everyone was eager to get underground again after varying degrees of holiday feasting. Met at stores, got pack pack packing, and left the union at 1948 hrs. Once in the minibus Tetley waggled his eyebrows at me as cavers are wont to do and said he had a Plan. A nice, long, wet plan. Now, I hadn't been caving very long but I'd already heard enough about Tet's infamous plans to know that I should be slightly concerned. Still, I found myself nodding agreement.</p>

