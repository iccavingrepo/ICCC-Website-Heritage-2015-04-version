After stopping at services one too many times we arrived at the Belfry with a sober Tetley, only to be greeted by drunk Aberystwyth (and some other Uni caving club, I forget) cavers, loud music, and the beginnings of a party. Nathan immediately took a liking to a machete that was lying about and proceeded to split wood with a vigour I've yet to see matched, while the rest of us drank tea and beer and watched the proceedings unfold.</p>

<p>To strobe lights and the strains of Hotel California, we looked on with amusement and mild incredulity as a couple of girls proceeded to strip, smother bright yellow paint onto their bits and be man-pressed up to the ceiling, leaving behind pairs of relatively innocuous-looking circular prints.</p>

