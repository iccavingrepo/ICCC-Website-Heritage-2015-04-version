<h3>Tet short round: Tetley, Nathan, Clare</h3>

<p>The next morning-almost-afternoon we got up, breakfasted, sat around talking about groups/trips till we meant it, and got changed. Thomas had already buggered off with aliens into Swildons.</p>

<p>I was to do Tet's Short Round with Tet and Nate. Now, we werenât the only group doing the short round. Ominously, however, we were the only ones steadfastly sticking to furries instead of wetsuits. Brave, or foolish?</p>

<p>Once in the cave we set off at a cracking pace downwards. Got to the 20 and Tetley belayed me and Nate down the ladder, abseiling down himself. Don't remember the details of the passages, but in my memory we were often walking through the river and I was really enjoying myself. The going was made much easier by the countless tips from Tetley - which route was the best, what to watch out for, which handholds to use and such - who, it seemed, knew everything about the cave: from passage names to the exact places where people dislocated shoulders or where stalactites were stolen from.</p>

<p>I remember once going head-first through a squeeze which opened out onto a slippery slope, and naturally I had to slide down it, get my helmet wedged between rock, half-submerged in water and breathing out of the corner of my mouth rather comically. Would have made for a brilliant photo-op, but I think Tetley and Nathan were more concerned with hauling me out.</p>

<p>Anyway, soon enough we found ourselves at the Blue Pencil. We plunged ahead and paid a visit to sumps 3 and 4. I was happy enough going down the Blue Pencil, but going up it was one of the twattiest bits of caving I've had to do. My wellies kept coming off, and Nathan finally had to bring one of them through the squeeze with him! Must have been a pain in the arse.</p>

<p>Back out in the main short round route we went on to have a look at sump 2 before making our way out via sump 1. The ducks, charmingly christened Double Trouble, were actually rather pleasing. Thereâs something magical about that first cold shock of water, then gently floating along on your back feeling utterly alone, face pressed up to the cool rock above.</p>

<p>After the sump was successfully navigated all that remained was one final push to the surface. By this time I was well knackered. I wasn't thinking much beyond the next step, barely conscious of how far we'd come and how much we still had left to go. Tet and Nate were pretty amazing through it all; incredibly patient and helpful - I think over the course of the trip I must have stepped on every part of them to get a leg up! After a particularly unpleasant and trying ladder climb, Tetley asked me, quite seriously, if anyone had ever told me I'm a bit mad.</p>

<p>We finally made it to the surface. Standing there exhausted and exchanging handshakes in the crisp night air, stars twinkling overhead, it felt as if an unspoken understanding had somehow passed between us. No one tells you this. No one speaks of the camaraderie that defines shared experiences, of how one may be soaked and shivering, yet filled with the most heartening warmth knowing that just for a moment, you belonged to something greater than yourself.</p>

<p>Yet how can they? I myself have tried to tell people what it is like, but even to my ears it always sounds flat. In many ways, it's part of the beauty of it all... I suppose some things are better left unsaid. It's like a bar of soap - the harder you try to grasp it, the easier it slips away.  </p>

<p>In any case, thank you Tet and Nate, for one of the best caving experiences I've ever had.</p>

<p>Oh, and that wetsuit/furry debate? All but certain Iâd take the furry again, you wetsuit wearing pansies.</p>

