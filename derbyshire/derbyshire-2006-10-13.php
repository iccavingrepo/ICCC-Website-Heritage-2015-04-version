<h2>Saturday</h2>
<h3>Giants: Natasha, Liz, Sophie, Sandeep, Lyndon, Tharatorn</h3>
<p>Heading out of London with the first set of freshers for 2006 we went north
to Derbyshire we hit the TSG at about 1am, the electronic code didn't work but
the Goths inside let us in anyway.  </p>

<p>Sparing the freshers the embarrassment of changing on the side of the road, we got into our furries in the hut and drove the few miles to Giants and P8. It was the girls and Tharatorn going underground with Lyndon and myself.</p> 

<p>We didn't miss the entrance to Giants this time and after a few photos at the entrance we dived in, not going the obvious route but instead doing an energetic 4m climb up to get everyone in the mood. A bit of exploring, walking round holes in the floor and crawling and we reached a dead end.</p>

<p>Back at the bottom of the climb, we headed on towards the ladder pitch but first were two remarkable chambers 15m high and 10m across, with white flowstone decorating one wall, an impressive sight for a first trip. At the ladder the freshers looked over the precipice into the hole that they would soon descend. After giving an initial briefing, Lyndon climbed down soon followed by everyone else as I belayed from above and gave the odd word of encouragement. The crab walk was fun, waddling along facing the wall, passing messages from the back to the front of the line by Chinese whispers.</p>

<p>We decided that we didn't want to get really wet so we turned around after a while and sat down for some Christmas pudding goodness. No-one had any problems getting up the ladder and it seemed like only minutes before we were out of the cave to be met by a phalanx of paragliders.</p>

