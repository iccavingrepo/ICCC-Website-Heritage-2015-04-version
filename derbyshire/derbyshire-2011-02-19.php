<h3>Hillocks</h3>

<p>For the first time Jan was the man to drive us to the Orpheus hut. Also for the first time in a long while neither Jarv or Clare were on the trip. So, what could possibly go wrong!?</p>

<p>Just before we left London discussions about possible snow force us to 'just in case' take to snow chains.</p>

<p>Journey was pleasant as always and on the way we picked up Gergely at the Leicester Tesco.</p>

<p>We were there before Midnight, still enough time to eat &amp; drink. I set up an alarm for 8.30, just because we also had to go to Caving supplies to get the expo rope and therefore to still start caving at reasonable time.</p>

<p>For some reason I woke up way before my alarm and could not fall back to sleep. It was unbelievable quiet. I finally decided that there is no point listening to others sleeping nicely, I looked out of the window - surprise! Everything was properly covered in snow and still snowing. Brilliant!</p>

<p>I was so excited and run out to jump around a bit before woke Jan and Co up to tell that we need to start getting the van out.</p>

<p>So super advanced team I,  Gergely and Jan spend some time putting snow chains on and trying to see how it all goes. Success! We can escape the hut and go caving.</p>

<p>In conditions like that (van being stuck in various locations and conditions) + picking up the rope was not surprising to finally park at Rowten farm at about 2PM.</p>

<p>Changing into gear soon turn into a flow of amazing events of personal gear not being present in the van. Kate started to alarm, that her bag is totally not in the van. After Jan's quick decision and offer to drive down to TSG hut to ask for some spare kit. Luckily for Giant's you do not need SRT. Beside conditions they were back reasonably quickly and Kate was super happy she can go caving. Literally a minute later Jan start alarming that his bag is totally not in a van. After who/what/where Kate on the end let Jan take the TSG kit and called her mum, which luckily lives close.</p>

<p>On the end at least caving trips were all great success and good fun. We had 2 teams one going down JH and other did a round trip in Giant's.</p>

<p>Back in the hut we quickly start cooking chilli instead of currie as agreed, as I for some 'unknown' reason at Tesco thinking of buying stuff for currie, bought everything for chilli. I did make up with making a delicious Frika form Cadrg cheese.</p>

<p>Evening pleasantly went on till 2.30. In the morning while planning caving rips, was quite clear that not all can go caving due to lack of 2 SRT gears + going to do ??? exchange + Kate urgently needed to go caving. So I nicely offered my kit to Kate and Jonny gave his to Jan.</p>

<p>All back safely on time, hut was cleaned meanwhile and off we went at 5.30 and safely arrived back to London at about 10PM.</p>

<p>Overall, we all had a great time!</p>

