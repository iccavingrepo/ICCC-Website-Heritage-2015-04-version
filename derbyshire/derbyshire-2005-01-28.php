<h2>Saturday</h2>
<h3>Giants: Darryl, Clew, Sarah Arctic, Nava, Dave, <i>Jarvist</i></h3>

<p>Right, as it was my first caving trip for ten months, I decided that it must be down this cave. It also coincided with 2 freshers needing to be 'broken in' on this trip. Perfect!
</p>
<p>That's what I thought anyway.</p>
<p>
<img align=left src="/caving/photo_archive/trips/2005-01-28%20-%20derbyshire%20-%20jarvist/IMG_0116--thumb.jpg">
<img align=right src="/caving/photo_archive/trips/2005-01-28%20-%20derbyshire%20-%20jarvist/IMG_0119--thumb.jpg">
Halfway through changing out of the back of the Minibus next to the trespass fee place for Giants, Clewin mentioned to me "I might go out early if I get cold...". That meant that I would be left leading the three tiny-tots along the round trip on my own with a dodgy shoulder IF CLEWIN GOT COLD! So naturally, this being Clewin the experienced Alpine exedition caver, I said "What?!". It'd turned out that he'd given his oversuit to a fresher [who managed to pack the smallest oversuit in stores - 5-foot Jo managed to squeeze into it on the Mendips trip with a fair bit of assistance, but was left with rather restricted mobility and a skin-tight fit], so would be caving in a bare Neofleece. Ok; could still be dealt with. Then I thought... Bugger. I didn't grab a spread from stores for the ladder... but a string of krabs will do. However, I then discovered that someone had taken the ladder out of the minibus the night before, and that it was almost certainly keeping itself warm in front of the fire at the Orpheus. Bang goes the round trip, so what else was there to do? [Considering we'd already paid our shocking 2-quid Trespass fees!]</p>

<p>Surprisingly, there was more than I expected...</p>

<p>
<img align=left src="/caving/photo_archive/trips/2005-01-28%20-%20derbyshire%20-%20jarvist/IMG_0155--thumb.jpg">
<img align=right src="/caving/photo_archive/trips/2005-01-28%20-%20derbyshire%20-%20jarvist/IMG_0163--thumb.jpg">
We went into Giants Hole, five in caving kit, and Jarvist (who wanted to see the real Giants entrance) in Dufel coat, cords and walking boots. Jarvist joined us until Waterfall Chamber, where we went to Garlands Pot and the crawl opposite. Sarah Arctic seemed to take a shine to climbing until meeting a shear calcite wall (fair do's), but Clewin had a go for 2 metres. That was all. We headed back to Waterfall Chamber, with myself trying a climb on the way (NOT comfortable!). From there, Clewin, Sarah and myself climbed upstream (similar to the Crabwalk) until the boulder choke, where we turned around and climbed up into the upper series and the nice traverses up there. Passed a couple of other cavers and found a hole down to Waterfall Chamber, but wisely backed up and climbed down a rift to meet Nava, Dave and Jarvist. We then headed out of the cave, a trip from which I expected less than I got!</p>

<p style="text-align:right">
Darryl Anderson
</p>

<h3>JH: Chris, Pip, Dan, Jo, Sandeep</h3>
<p>Following my last caving attempts in two left wellies, I concentrated really hard at  selecting kit this weekend. Only mistake this time was thinking a dinky elbow pad would work a knee pad. Hmm... Anyhow, (as Sandeep has mentioned) I was persuaded into thinking a 90m trip down J.H> was what I fancied doing. Well, maybe not <s>fancied</s> doing, but I thought it sounded more doable than the initial -200m suggestion. Frist pitch we all sped happily down, first time I've noticed my descender get hot! Pip went on ahead to rig the second pitch whilst the rest of us followed on behind. During this brief respite, I had enough time to scare myself into thinking that the first pitch was quite long enough and that I'd never be able to reach surface again if I went much further down (Hey! I'm a feeble <i>fresher</i>, I'm allowed such thoughts!). Sandeep and I both decided to abort + head out.
</p>

<p style="text-align:right">
Jo King
</p>

<p>[Ed: <i> Chris and Pip continued down the next pitch; had a look around, and with beautiful timing; came flying out of the entrance with their nitro-assisted footjamming speed just in time to hand over some of the tacklebags to the lethargic freshers. Out in time for sunset, then back to the Orpheus to help with the curry.</i>]</p>

<p>Pip said it [Ed: Some kind of pretty mineral deposit...] was the only placce in the world where it could be found. The traverses as far as Bitch Pitch was beautiful, over crystal clear water which went down and down! I couldn't do the traverse over the top of [aptly named] Bitch Pitch, and so caught up with Jo + Dan to follow them out. A pity, I should have just trusted the rope and dangled over - Chris and Pip said the bottom was really nice. So we all ended up out in record time, a good Omen for Grenoble!</p>

<p style="text-align:right">
Sandeep 'Stan' Mavadia
</p>
