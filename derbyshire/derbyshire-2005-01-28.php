<h2>Sunday</h2>
<h3>Giants take 2: Darryl, Chris, Sarah Arctic, Nava, Dave</h3>

<p>Equipped with the ladder this time, we piled out of the minibus at the farm
into a delightfully icy wind and trundled back off to the cave entrance, to
find that Dave's light didn't work. After some quick thinking handy work
from Daryl we were back on track with 5 illuminated torches, and all bombed
down the ladder. At this point Daryl pointed out that there was a way of
getting down by placing a handline, so actually we could have done the cave
yesterday as we had a rope with us...
</p>
<p>Onwards twisting up, down and round the crab walk, through the vice and
surprisingly quickly we were at the half way point. A short walk down a
passageway took us to the connection to oxtale (?) - a long, horrible tight
crawl feet first on your back apparently.. so we swiftly turned back and
began the second half of the round trip! Everything was a more open on the
way back as we moved over the top of the crab walk. There were a couple of
climbs using handlines, and, delightfully, a considerable belly crawl
through muddy water which involved a few worrying moments of having to move
your head into a position sideways enough to breathe, yet forwards enough to
see where to go... Crawling through chocolate I told myself, pleasant, warm,
hot chocolate...
</p>
<p>Next was traversing, which aside from the 70ft drop I quite enjoyed, until
we came to the last part. Here the ledges sort of ran out, so you had to
kinda wedge yourself and lower yourself down and across to the edge, in that
effortless seeming way cavers do, all the while over the 70ft drop. Not my
strong point, Dave mentioned that he found vertigo in the cave much more of
a problem than claustraphobia, and it strikes me that this is bizarrely very
true. Anyways, in my efforts to remain wedged I moved far too close to the
top and ended up relatively stuck to the ceiling, my exit was via pretty
much kicking and then standing on Daryl's head, he was very good about this.
Next was an equally interesting sideways crawl through the eye hole, which
was easy once you believed you wouldn't fall down the big hole. I find it
surprising how often caving is more of a psychological than a physical
battle. We were all still soaked to the skin from the crawl at this point,
and with Dave and I taking some time over these bits we were all rather cold
from waiting. Luckily Nava sped through and we got off moving for more
traversing and a climb down before re-climbing the ladder and seeing the end
in sight! Luckily we exited the cave safely with all arms and legs in tact
as there was not even a hint of the strange duffel coated monster we had met
the previous day.
</p>
<p>Like clockwork the minibus was driving down the road to us as we walked back
up to the farm, and we were whisked back to the cave hut in true chauffeur
style to very tasty cheese-and-a-variety-of-fillings sandwiches (thanks
Jarv) - just what the doctor ordered before our return to bonny home!
</p>
<p>
Many thanks to all for your fresher patience, and particularly to the
drivers for dropping Sarah Arctic in Oxford.</p>

<p style="text-align:right">
Sarah Arctic
</p>
<h3>Nettle Pot: Clewin, Dan, Jo, Sandeep</h3> 

<p>We set off with a purposeful stride which rapidly changed into a searching meander once we realised that no-one actually knew where we were going. After much peering into any likely dip on the way, Clewin thankfully bumped into the entrance hole at the top of the hill. 
</p>
<p>
Dan rigged the first pitch with a reassuring "I've never done this before" whilst the rest of us all huddled out of the wind just inside the entrance. We descended as far as Grand Canyon before doing a sharp about-turn and heading back. The tight pitch made it slightly slow going but the top appeared surprisingly quickly (in my view anyhow, - I'm sure for those waiting it wasn't so rapid..!) I was first up and so, whilst waiting, I amused myself by trying to retrieve my gloves from somewhere down the back of my oversuit. This involved various contortions and finally proved to be a two man operation once Sandeep arrived to aide and abet. 
</p>
<p>
We emerged from the cave to "a delightfully invigorating breeze" (A slight translation of Dan's words there) and with perfect timing we managed to return to Giants in coordination with the return of the other group. 
</p>

<p style="text-align:right">
Jo King
</p>

