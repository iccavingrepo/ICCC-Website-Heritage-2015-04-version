<h1>Derbyshire</h1><h2>Weekend Trip 3/4 May 2003 </h2><i>Report by Clewin</i>
<p>Pete and I went to Derbyshire with the sole intention of doing something
other than Giants, P8 or JH, and had a cracking good time with some of the
WSG lot.
</p>

<h3>Saturday</h3>
<b>Eldon Hole</b>
<p>We decided to do Eldon hole, which involves a reasonable amount of ropework. 
   Things didn't look too promising when we parked the car at the 
end of Eldon lane and realised that we'd forgotten to bring the hangers.
Luckily I had enough bits of metalwork lurking at the bottom of my kit bag
that we could do it anyway.
</p>

<p>
   There are a number of routes down the open shaft (45m?), 
some of them rigged off rather dubious looking posts. At the bottom, there's
a small hole to crawl through which leads to a reasonable large chamber.
The only sensible way on is upwards.
</p>
<p>
Rigging this is fun: there's a pully
system with a piece of green cord. Somehow you have to attach your
rope to the cord, and pull it up the pitch, hoping it threads itself through
a large metal ring at the top. After half an hour of trying (we weren't going
to be defeated) we managed to get the rope through, using one of my
boot laces to lash the rope onto the string. 
There are some nice decorations at the top, which are generally unspoilt
since it's so difficult to get up there.
</p>

<h3>Sunday</h3>
<b>Hillocks / Knotlow mine exchange</b>
<p>We split into three groups and tried to do a rather ambitious exchange between two Hillocks and
   two routes down Knotlow mine. I rigged down the main shaft, adding in
a short diversion to where Hillocks pops out the side of the shaft. 
The two Knotlow mine teams met at the bottom and went for a stroll down
various water-filled passageways.
</p>

<p>
The connection from Hillocks to Knotlow is anything but obvious from the
Hillocks side.  Unfortunately, what I thought was the connection at the
side of the main Knotlow shaft just turned out to join back up the 
other Knotlow route.   Pete swung into the real connection and found his way
out of Hillocks. After listening to his description of bedding planes full of water,
we weren't that disappointed that we hadn't done it.
</p>

