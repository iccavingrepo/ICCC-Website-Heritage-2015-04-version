<h3>The mystery of the overhand...</h3>

<p>So there I was, down stores again for an evening (procrastinating from writing up? me?). Unpacking a rope back, I come across a curiously dense knot. Ten minutes of aggressive wetting, pushing, pulling and scrunching, and I have it loose enough to see what it is. It was indeed a simple double overhand knot. That someone abseiled off. Mmm.</p>

<p>Other than the age it took to undo, the rope was actually fine underneath it, the core not kinked at all. It would be such a curious knot to use intentionally, that I haven't actually been able to find any recent rope test data to see how weak it is. But it certainly is difficult to undo, and the much chastised learner rigger will never be forgetting how many turns to put in the double figure eight!</p>

