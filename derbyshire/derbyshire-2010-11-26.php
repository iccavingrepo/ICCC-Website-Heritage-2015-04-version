<h2>Saturday</h2>

<h3>Oxlow->Maskhill: Clare, Nico, Kai, Charis and Kate</h3>
<h3>Maskhill->Oxlow: Goaty, Jan, James Roberts, Nia, Kangyu</h3>

<p>Headed by the danger trio of Clare, Nico and myself we were dropped off by James KP 'driver extrodinaire' along with the wonderful freshers left in our care, Kai and Charis. Our mission: to go down Oxlow, exchange with the other group and leave through Maskhill without death/serious injury. We quickly found the entrance on the cold and snowy hillside and after vast quantities of faffing began our task. Clare with the assistance of Nico bravely took charge of the rigging whilst I took up the rear. After waiting around in the snow and freezing winds we descended into the familiar mine shaped pitch of Oxlowe. </p>

<p>Thankfully our frozen hands quickly warmed and became mobilized in the shelter of the friendly cave. We soon ran into a group of Mendip cavers going the other way and whilst waiting for the groups to pass each other we exchanged words regarding ladders and horror stories of 2nd year led trips. After smoothly passing the first few straightforward pitches some confusion arose regarding the direction of the way on, Clare's thorough exploration of a humungous chamber revealed that it was just behind the bottom of the pitch. </p>

<p>This led to a slopey passage requiring a 55m hand line that caused much annoyance for the rigging duo as the rope we had brought wasn't long enough. Turned out they started it too high up the passage. They tried every combination of knots and arrangements but we ended up using the old permanently rigged hand line on the last section, which was fine. The last pitch, rigged by Nico, was quite nice with another lovely deviation. Whilst descending I am harassed by Clare 'Hurry up!' she shouts, we had found the Maskhill team. </p>

<p>Chocolate, a piss and a potter were abruptly followed by the ascent of Maskhill. This was the part I was not particularly looking forward to, the 150m of pure prussicking with a bag filled with bulky 11mm rope. The two freshers were in front which left Clare, Nico and myself to have a good laugh, reflecting on the ridiculous situation we were in. Clare (my hero) took it upon herself to derig and we went on our way. After the first pitch an entertaining swing pitch provided some fun, with no one managing an elegant execution. Then came an adorable traverse before a large series of small to medium pitches. </p>

<p>Not sure if it was because it was my second visit or because the cave is spacious and dry but the cave felt very friendly and comfortable. Lots more pitches. Fun fun fun. Didn't want to leave the lovely snuggliness so waited around and had a singsong with Nico. When the presence of Clare was felt at the back we hurried on out into the bitter cold. To the van we went, cookies and apple pies we found. Clare stole items of clothes from everyone and even had the courage to nick Nathan's super coat [a brand spanking new down jacket...]. Nice walk through Winneti's Pass back to the TSG where Thomas had prepared fantastic Bolognese. The result: a nice nostalgic Oxlow-Maskhill trip without death or serious injury. Not thanks to Nico though as it turns out he rigged his pitch with a double overhand. Let's just hope the adults let us do it again some time.</p>

