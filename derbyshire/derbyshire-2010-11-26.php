<h3>JH wave 1: Nathan, Ginger Martin, Mehdi</h3>

<h3>JH wave 2: James KP, Ari, Jean, Thomas MCW</h3>

<p>The plan was for me to pick up the late stragglers and follow Martin,
Nathan and Mehdi down JH. We wanted to visit the white river series
from JH. Ari, Jean, Thomas and I reached the cave at 3pm. We got
changed in the minibus and wizzed down the first pitch. I warned folks
not to go swimming in the submerged works, soon enough we got to the
top of bitch pitch, where we met Martin. Apparently the rope was a
little short for the very bottom and we needed to transfer onto a
reasonably healthy looking permanently installed rope that we had been
told about by a member at the TSG. At this point Thomas was not
feeling too well, so I escorted him back to the entrance. I rejoined
the party on Leviathan which was swiftly descended. Then the fun
started.</p>

<p>The canal towards speedwell was cold. We tried to avoid the deeper parts and
ended up on a pointless detour upstream of a small tributary [Ed: suspect this was whirlpool passage? reports of wading up to chest deep, except for Ari who was up to her neck!]. Soon enough
we retraced our steps and embraced the cold cold water. Soon we
reached the bung:  a ladder in a waterfall. Nice. Immediately after
this was block hall, a dry oxbow with a rope hanging down. Nathan was
first up and we all followed. It was taking ages to get up this
bastard, and what with its pendulums, hanging rebelays, considering
the time and looking at the faces of my companions I made an executive
decision: back out!</p>

<p>So started the long way out, eating chocolate bars and freezing in the
cold cold water. Got out to a crisp night. The weather was
sufficiently cold that the [cave pad]lock was frozen shut. Seriously? Got back
to the minibus to inspect the damage. Mehdi was asleep. Nathan was in
a sleeping bag and hogged the heater. Time to get back.  The hut was
deliciously warm the food tasty (thanks Thomas) and everyone was
asleep. Good times!</p>

