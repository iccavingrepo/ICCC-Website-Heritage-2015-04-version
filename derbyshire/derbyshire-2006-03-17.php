<h2>Saturday</h2>
<h3>Winnat's Head Cave: Pip, Chris, Jarv &amp; Rik</h3>
<p>
Entrance was beautiful, at the top of Winnat's Pass (just on right past cattle-grid gate heading towards Castleton, under obvious rock arch), with fantastic ice stalactites, helicites &amp; a beautiful ribbed entrance chute of ice-pebbled moss. Managed to mess up the entrance, by going a gung-ho feet first, eventually getting stuch &amp; finding myself face-to-face with Chris, before forcing him to do a reverse crawl up hill until we reached a turning point.
</p>
<p>
Overall, turned out to be a fairly easy (but really rather dangerous for
the unwary) cave, which involved clambering down through an enormous
boulder choke. If you picture a load of lego in a jamjar being shaken
about and then allowed to settle, before sending in Ant explorers to
squeeze and crawl around towards to the bottom, then you have a fairly
accurate picture. Except replace the lego with blocks of limestone,
sized fairly continual between that of your fist &amp; a semi-detached
house, and stick the whole together with mud, and you'd be spot on.
The start was the most worrying, as we followed a hand-line (rope)
down through the boulders,  whereupon it
suddenly disappeared under a fridge sized boulder, leaving us to climb over
what had been the ceiling not so very long ago.
</p>
<p>
But after all the fear, and slow care to make sure we didn't bring the
whole house of cards down around our ears, we burst out into an
enormous stone cathedral, with a beautiful fault line dividing the
ceiling, the giant rocks making up the floor topped with circular dots
of slippery orange flowstone, proto-stalagmites slowly reaching up towards the slow drip from 30m above.
</p>
<p>
More boulder chokes, a rather committing slither down a rift with the
approximate dimensions of a letterbox, and a champagne-cork-esque
squeeze through a wet hole in the floor, and we found ourselves at the top of a gentle
clamber down to an amazing underwater lake - a good 10m or so across. 
Perfectly flat, save for a
ripple made by the occasional drip, the light from my helmet scattered
and bounced off the crystal-coated domed roof. All the mud was
settled, and it was amazing clear, quickly slipping away into frigid
inky depths. A little tributary sent a tongue of muddied water
skimming gently across the surface, before gently dropping in a cloud
of brown.
</p>
<p>
A manual pump, the rusted metal seemingly dating to the Victorian era, sat in the shallows. Though the tubing was flaking of in our hands, it was still capable of squirting a feeble jet of water. I assume some rather desperate explorers must have attempted to bail this sump at some point! Bailing a swimming-pool doesn't really appeal...
</p>
<p>
The cave continued, but involved a crawl through the aptly named
'Sewer', as rubbing our chests in snow melt water didn't really seem
like the most amusing way to spend our 4pm 'tea time', we headed out,
pausing briefly to eat the finest Chocolate known to humanity (50p per
bar Tesco Value solidified vegetable oil sludge).
</p>
<p>
Rik, while waiting for a rope to be attached to some anchor points,
decided after having tried throwing the mud and making mud sculptures,
that clearly the next step was for him to give himself a mud facial. Out in time for Dusk, and a slow trudge back to the minibus.
</p>

<h3>Oxlowe: Everyone else!</h3>
<p>
Sandeep doing some rigging practice, didn't get as far as had been hoped. Out in time for a quick pint at the local pub, before returning back to the Orpheus to cook a massive Food-For-Thought-esque not-so-healthy stirfry.
</p>
<h2>Sunday:</h2>
<h3>P8: Shed, Deep &amp; Jarv</h3>
<p>
After a bit of a sludgy trudge through a field that appeared to have been used for a quad-bike race, eventually found the big shake hole, dotted around with trees in the middle of a field to the rigght of the foot-path.
</p>
<p>
Entrance was a rather wet jump following the water down a 3m deep hole, followed by wriggling quickly past the waterfall and stumbling on in some lovely cascade passageway. First pitch is best rigged to P-hangers 3m below the pitch-head, then traversing onto an easily passed sloping ledge to the left. Way on is to remain high - so no point descending into the spray only to climb back up! 
</p>
<p>
Bit of a crawl, then a clamber, then a few careful bold-steps over 5m drops, before reaching a pitch-head at the top of a big lump of flowstone. Backup was a bowline around a stal the over side of a drop, then to Y-hang P-bolts. Helps to have long legs to reach while standing on Flowstone! Quick 5m drop to larger passage, many ways on. In particular, going straight forward and through ~20m of confined rift, burst out into a nice large chamber with more ways up. 
Had a bit of a ferret around, then exited into sunset &amp; to find-out our collection of Jaffa Cakes.
</p>

<h3>Maskhill Mine: </h3>
<p>Rigging-learning trip for Dan. Things going well, when Rik was caught-out once again (yes, on a Sunday trip, in Derbyshire!). Dropped the pitch quickly, whipped off his clothes above the 50m pitch following, and found a new use for the pristine pair of marigolds that I had given him on Friday. Dirty dirty boy. Once back on the surface, he disposed of his produce, and did his best to polish himself more fully with capacious amounts of snow.
</p>
<p>
Finally leaving in the gathering gloom, a careful trundle to the nearest petrol station considering the blinking light on the dashboard, and then an entirely clear-road drive home via Oxford.
</p>
