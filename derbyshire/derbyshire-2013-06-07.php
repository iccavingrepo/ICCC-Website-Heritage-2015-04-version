<h3>Dive report for the CDG Newsletter</h3>
<tt>
PEAK CAVERN, CASTLETON, DERBYSHIRE SK14898259<br>
<br>
INK SUMP<br>
9-6-13<br>
Divers: Jarvist Moore Frost, Clive Westlake<br>
Sherpas: Imperial College CC<br>
<br>
With the promised assistance of a team of Imperial College cavers, JMF and CDW chose to dive with the large air margin afforded by 7 litre cylinders. The sherpas whisked the six tackle sacks through Peak Cavern directly to Ink Sump, enabling JMF and CDW to submerge 80 minutes after entering the show cave. </br>
<br>
Ink Sump was a lovely dive, with almost perfectly visibility for JMF at the front, with just odd wisps of suspended silt in the water. The 8 mm beige line is extremely well belayed, with numerous lead and bolt anchors. The final section of line between the last airbell and Doom's Retreat is currently trapped under a circa. 5 metre long section of split cobbles from landscaping in the aven. <br>
<br>
Beyond the sump, CDW and JMF dekitted, ascending the fixed ladders to admire progress at the dig front, and take advantage of the hospitality at Jim's house.<br>
<br>
Warmed by hot Ribena, CDW and JMF kitted up for the return an hour after surfacing. JMF was sensibly instructed to wait on the line after the trapped section till reunited with CDW. Visibility on the way out was an acceptable 2-5 metres, except for the last 30 m approaching Ink Sump where it dropped to below a metre.<br>
<br>
Many thanks to some extremely able portering by Imperial College CC which made this such a pleasant and low effort dive, and Jim Lister for arranging access and hospitality at short notice.
</tt>

