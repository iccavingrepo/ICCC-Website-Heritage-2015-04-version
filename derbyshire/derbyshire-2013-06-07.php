<h2>Saturday</h2>
<h3>Maskhill <> Oxlow</h3>
<h3>Mashill -> Oxlow: Oli, Jarv, David KP, William</h3>
<h3>Oxlow -> Maskhill: Nico, Izi, Riaan, Giulio, Saber</h3>
<h3>Oxlow <> Oxlow: Su</h3>

<p>Trip made slightly more challenging by a rather odd assortment of rope from London. We permutated as best we could during the morning at the chapel, finding the ropes in Maskhill persistently finishing frustratingly halfway down pitches, yet having just enough meterage to reach the bottom with skipping p-bolts and expo-rigging to ensure minimal rope consumption.
The Oxlow team, strangely, had a spare rope when they got to the bottom!</p>
<p>This was Su's first SRT trip, so she didn't try the traverse, but took some photos in the main chamber before heading out Oxlow.
Surface to glorious sunshine, and an efficient return to the TSG before JMF stole the minibus and went to see the CDG at their Orpheus retreat.</p>

<h2>Sunday</h2>
<h3>Peak Cavern - Ink Sump</h3>
<h3>Glorious Porters: Giulio (until the 2nd muddy duck), Riaan, Izi, Saber, Oli, Su, David KP</h3>
<h3>Divers: Jarv & Clive Westlake</h3>

<p>A too-early start saw us squeezing into damp kit at the chapel while slurping tea. Six heavy tackle sacs of gear (Clive and JMF were diving on 7L cylinders) were whisked away and into the cave, allowing the divers to swim off into Inksump within 80 minutes of entering Peak Cavern. Giulio baled at the 2nd muddy duck (unfortunately, the last wet place!), with a curse on wet english caves. The dry cavers then went to explore the region beyond 'Surprise View', checking out Far Sump.</p>
<p>The porters returned 5 minutes ahead of the divers returning, and again, the gear was efficiently whisked away and out of the cave as fast as it was removed.
All were out for 3PM, to bask in the sunshine, make do on their lack of morning fried breakfast & effect an early return to London.
</p>

