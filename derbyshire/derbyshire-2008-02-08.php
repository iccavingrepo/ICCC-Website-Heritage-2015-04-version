<h2>Sunday</h2>
<h3>Oxlow / Maskhill: Jan &amp; James KP</h3>
<p>The next day I was keen to practice some derigging, luckily Jan was
happy to supervise me and Dave to drive us to Oxlow. Went down the first
pitches of Oxlow and of Maskhill, practiced rigging without making any
deadly mistakes (I think!). Maskhill mine looks quite interesting and it
was a shame that lactic acid, the time of day and the lack of ropes
stopped us from looking further. In the meanwhile Ben and Rik where at
the hut, cleaning up and bumming around, Tim, Jana Jarv and James H.
where walking about Tor something-or-other: a strange reverse cave with
rocks in the bottom and empty stuff called "air" on top: the weird
things some people do!</p>
<p style="text-align:right">James Kirkpatrick</p>


