<h2>Saturday</h2>
<h3>JH -> Titan: Andy, Chris, Pip, Jana, Jarv</h3>
<p>
Having got the key from Rowter Farm, I had time to change, rig the entrance pitch to JH as well as a little snooze in the sunshine before the rest of the party turned up.  With 5 going down JH and 7 down Titan, the scope for faff was immense, so we set off at full speed.</p>  

<p>I've done JH a number of times now, so the usual greasy scaffolding, big pitch, hanging death, crawling on rubber, hanging death, big pitch etc etc etc followed each other until I'd rigged to the bottom of Leviathan.  With Pip just behind we dived head-first into the grotty crawls on the way to the bottom of Titan.</p>

<p>A few wet pools and a lot of grit later the five of us met up for the first time since the surface, had a bite to eat and warmed ourselves on my perfectly working carbide (having finally disabled "suicide" mode).  Pip assured us she knew the way to the bottom of Titan, so we spent the next hour or so wading through water and mud (is this the right way? "PJW 1985" says not.  In hindsight we possibly found the Far Waters sump, now bypassed by a dig the other direction from Leviathan).</p> 

<p>Anyway, we met up with the Titan party somewhere in the crappy horizontal bit.  Pip clearly did know the way, as by the time the other four of us had got to the bottom of Titan she was out of sight beyond the Event Horizon.  Prussiking out of Titan is fairly easy, certainly easier than JH.  Apart from getting cold feet waiting at the top with my feet in the puddle and the rope spaghetti at the single rebelay, the only real excitement was finding damage to the rope about half way up the top half.</p>

<p>A bit of advice for communicating on big pitches - don't bother.  If someone needs help it'll be obvious from the screaming, otherwise you should just ignore all other forms of communication apart from "rope free" and "OK" unless you want to confuse yourself.  Solo trip back across moor in vain attempt to find JH in dark.  Finally found James prussiking out, and assited Rik and JKP derigging.</p>
<p style="text-align:right">Andy Jurd</p>


<h3>Titan -> JH: Rik, James H, James KP, Tim O, Dave W, Jan, Ben B</h3>
<p>Saturday we visited two amazing caves: 7 people (Dave, James
Kirkpatrick, Tim, James Hugget, Rik, Jan and Ben )   absailed down a
giant hall: Titan - the largest single shaft in the UK with it's 140 m -
and 5 people (Jarv, Jana, Andie, Pip &amp; Chris) down John Hall over Engine
Mine (aka J.H.). </p>

<p>Titan was rather sphincter clenching: leaning over the
edge and starting descending Titan took a bit of suspencion of disbelief
("I will not be fall into the abyss"), but not as impressive as some
pics would suggest: it's basically a big, big, fecking big hole. Since
there were 7 of us waiting for everyone to get down involved a lot of
waiting...</p>

<p>Dave had quite presciently brought some whiskey, while James
H. and Ben had rather un-presciently not brought waterproof oversuites:
they must have frozen their asses off! At the bottom of it  a boulder
chokeand a bit of walking  lead to a bottom level. Here we met the J.H.
team and continued walking until we reached a a largish stream, which we
followed up-stream until it became a flat out crawl. Eventually this
emerged in a larger passage which lead to a large hall, where we found
the ropes left by the J.H. team.</p>

<p>We hosed ourselves down, trying to get
all the grit and mud out of our jammers. We then started prussicking up
"Leviathan" a much smaller shaft than Titan but still rather impressive.
The top 30m or so of Leviathan where very wet with spray coming off the
rock. The feeling of looking up and being confronted with nothing but
spray and water was not too pleasant... </p>

<p>At that point I was a bit tired
and my memory gets a bit hazy... I seem to remember some pretty nasty
rocky walking on all fours dragging the ropes behind. Why did i
volunteer to derig? More importantly, why are ropes so heavy? At that
point we were in a disused mine and that the ceiling was held up by some
rather worryingly ancient looking bits of wood.</p>  

<p>Eventually we reached
the next pitch: 40-45 m up a narrow crack aptly named "bitch pitch".
Having climbed up the rope and de-rigged (providing Rik with more weight
to drag along... ), we reached a beautiful passage with deep clear
ponds. I asked Rik how deep they were and he answered "they've never
been bottomed"... mmh... since the pools were roughly as wide as the
passage I paid a lot of attention and tried to stick to the sides of the
wall! </p>

<p>We reached the entrance pit: just another 40-50 m up a
vertical shaft and out to the surface. After 8 hours underground what
a majestic ceiling greeted us: the night was clear and the sky strewn
with starts. Andy was waiting for us, happy that he had now been to Titan,
as the next day he would announce his retirement from caving, seen it all
apparently!</p>

<p>Back to the Orpheus hut where Jan and Dave where busy
preparing a delicious meal. I scoffed my plate down, downed a beer, sat
in front of the fire and passed out... mmhh sweet oblivion. I think it's
safe to say everyone was satisfied with this bonza trip!</p>
<p style="text-align:right">James Kirkpatrick</p>

<h3>JH - Titan by Jana</h3>
<p>At first I though I'm gonna go down Titan and up JH. But after Andy's argument, about how easier is to go up just in one go, I changed my mind. First time I have been down JH and I must say it is one of the most beautiful caves. Easy pitches, lovely small crawls, a mine working museum and shiny calcite crystals. On the bottom, we start searching for the connections to Titan. Fist time we went the wrong way and end up walking in super muddy small lakes. On the right way, half way threw the connection, we met the other group. </p>

<p>And there we were at the bottom of this huge blackness - Titan. It is hard to say how impressive it is - you can not really see it all in it's beauty. Going up is actually nothing special. It is just you and the rope and the darkness all around you. Looking down, you can hardly see a light from a caver waiting behind to follow up. Only one confused - twice rigged rebely. You had to think twice which rope to take to continue going up.  </p>

<p>I would only go back to Titan if it was lit up, but will definitely be back to JH.</p>

<p>Jarv and I were the last peoples who were all alone finishing derigging and locking up the Titan. It took us more than half an hour to lock it! We had the keys and we had to find a locker hidden in a chest. We spend 10 min looking for chest, hidden behind a big pile of dirt 10m away from the entrance. And then like puzzles putting things on place. </p>
<p style="text-align:right">Jana Carga</p>

