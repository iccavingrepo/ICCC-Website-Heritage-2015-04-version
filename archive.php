<p>
<img src="slovenia/slov2010/2010_vrtnarija_below_500_new_passage.png" align=right>
Vodna Sled 2010 was an epic success - in all we discovered 2.2km of new passage, all below -500m, all in Vrtnarija using Camp X-Ray (Reloaded, four bed camp) -550m as a base. <br>
