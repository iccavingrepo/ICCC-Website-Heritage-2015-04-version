The dregs of the house party from the night before managed to pour themselves onto the tube + eventually make it to PADDOCK, the Alternative Cabinet War Room in NW2.
</p>
<p>There is a nice bit of background to the site 
<a href="http://www.subbrit.org.uk/sb-sites/sites/p/paddock/index1.shtml">here</a>.</p>
<p>
After getting lost + arriving late, we managed to liaise with Sub-Brit, who had a guy to give us a guided tour of the complex. Was a very interesting mix of caving skills + history, some bits of the bunker had been converted to other uses since the war, some rooms were almost untouched - the air filters were still present into the ventilation system.
</p>

<p>Pics of the trip <a href="http://www.union.ic.ac.uk/rcc/caving/photo_archive/trips/2004-10-30%20-%20urban%20exploration%20-%20paddock%20CWR%20london%20-%20jarvist/dirindex.html">Jarv's</a>, <a href="http://www.union.ic.ac.uk/rcc/caving/photo_archive/trips/2004-10-30%20-%20urban%20exploration%20-%20paddock%20CWR%20london%20-%20lyndon/dirindex.html">Lyndon's</a>, edited highlights of the party + clues as to why Clewin is wearing someone's elses clothes <a href="http://www.union.ic.ac.uk/rcc/caving/photo_archive/trips/2004-10-29%20-%20309A%20house%20party%20-%20jarvist/dirindex.html">Jarv's</a>, <a href="http://www.union.ic.ac.uk/rcc/caving/photo_archive/trips/2004-10-29%20-%20309A%20house%20party%20-%20lyndon/dirindex.html">Lyndon's</a>.</p>
