<?php begintextdatebox("Urban Exploration", "Saturday 30th October, 2004");?>
<p>
<a href="photo_archive/trips/2004-10-30%20-%20urban%20exploration%20-%20paddock%20CWR%20london%20-%20jarvist/IMG_0445.html"><img align=left src="photo_archive/trips/2004-10-30%20-%20urban%20exploration%20-%20paddock%20CWR%20london%20-%20jarvist/IMG_0445--thumb.jpg"></a>
