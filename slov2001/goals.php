 <h3>Gardeners' World</h3>
 <p> Well this is the obvious one. Zlatarog rift. A vast expanse of blackness at the limit of the known 
 universe...</p>
 
 <h3>Pri Madoni</h3>
 <p> Pri Madoni has only been partly surveyed, and not fully explored. Additional exploration
 by the JSPDT means is is now around 500m deep. </p>
 
  <h3>Surface Surveying</h3>
  <p>Since we have a number of different leads on the surface of the plateau, a comprehensive
  surface survey is really needed. As well as using traditional techniques (tape, compass, clino)
  this may also involve use of GPS now selective availability has been switched off.</p>
  <p>Possibly even more important is a log of the positions of blowing holes and cave entrances
  and a photographic record of each for identification.</p>
  
  <h3>Dye Tracing</h3>
  <p>Unfortunately, bad weather last year (the worst since records began in 1995)
  meant that the dye tracing attempt was a failure. One of the main problems is
  that we are trying to trace water in small mountain streams which can easily dry 
  up or flood. A result in 1999 indicates that the water from the Migovec plateau
  actually resurges in the Mala Savica. This is not at all what was expected, and
  has to be investigated further.</p>
  <p>The main obstacle to verifying the result is the fact that the Mala Savica is 
  below Dom na Komni, a hostel which could be a source of contamination. 
  Therefore we need to do a control test as well as a proper cave dye trace</p>
  <ul>
  	<li>At start of expedition, put in detectors in Mala Savica</li>
	<li>Leave for ~10 days and collect, replacing with fresh detectors</li>
	<li>Introduce optical brightener at main streamway near Hotel Tolminka (-600m)</li>
	<li>Check detectors after a suitable time has elapsed.
  </ul>
