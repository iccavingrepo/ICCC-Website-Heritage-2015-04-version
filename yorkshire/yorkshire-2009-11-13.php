<h2>Sunday:</h2> 
<h3>Bullpot (Kingsdale): Jarv, Jana, William, Steven, Aleeza</h3>

<p>I had never actually been to the bottom of Bull - prior trips either turning back before the Slot due to high water, or failing to find the entrance entirely either in the hill fog, or the fog of incompetence. The entrance was already rigged, so Jana put our rope down on the opposite side of the shaft. </p>

<p>The other party turned out to be on some kind of leader training program, so while they practiced rescue haulage on the slot we slipped by them down the alternative third pitch and continued. The bolts are rather more minimalist down this alternative route, but a few Ps and the odd stainless Petzl permanent thingy found us back on track. The fourth pitch was dropped with a minimal number of P-bolts being used, as we were by now short on both rope and maillons! A long sling and a fairly alright deviation saw us to the bottom, with just enough rope spare to tie off to the bottom P-bolt. Something like a 32/56m combination saw us without a break from the top of the 2nd, via the alternative 3rd and down the 4th.</p>

<p>Way out was really smooth and quick, everyone out while it was still light and prussicing up the shaft while the trainee leaders learnt ladder technique. Back at the van shortly after the Yordas lot (minus James the derigger), which was nice, as they had the van key and it started raining.</p>

<p>Little bit of a wait for the Aquamoles to return (long after dark), then a quick hut clean and then back in the van to zoom back to London.</p>

