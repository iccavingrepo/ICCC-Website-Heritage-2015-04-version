<p>Biblical floods were predicted, but by the time we had reached Birmingham the clouds parted, at least for a while. During the night a good 6 inches fell (observed scientifically by Dave Wilson utilising a pint glass left outside). So we were expecting it to be rather damp....</p>

<h2>Saturday</h2>
<h3>County Pot: Jarv, Steven, Mustafa, Kate, Alex</h3>

<p>It had been a good few years since I'd made it down County last! We were in the way of a gang of York Uni cavers so I shot ahead to the pitch to get it rigged and left the freshers to find their own way, which they did. The pitch was duly dropped, and they went off to clamber down into the little streamway. Once everyone was together we skipped through the shower-bath and down the rather well washed cascades. Spout Hall wasnât actually too bad, so we took it directly rather than making use of the bypass. Poetic Justice was rather a challenge, as it was pretty well lubricated with drips, and my PVC Meander was pretty slick! So after I got up we rigged a little bit of 9mm off a stal boss and the rest self-lined. The short crawl was wriggled along and some fairly nice black rope was found in-situ found rigging the Y-hang. Our little bit of 9mm formed a traverse, as we dropped down to the little rivers. </p>

<p>First we went upstream from the junction, the stream and cascades really quite pretty, and the white line chamber really quite impressive with the amount of water coming down. We headed back to the junction and stopped for lunch, before following Kate downstream and quickly finding ourselves in the big complicated wet area. Eureka junction was quite spooky - with great fluffy wreaths of foam collected in the scallops in the ceiling. </p>

<p>Still, the water levels were quite low, so we headed down and then upstream to wriggle through the rather unpleasent not-quite-flat-out crawl into Stop Pot. Loitering at the bottom of Stop-Pot, we noticed lights coming down from up-high. Assuming it was the Yorks lot coming through from Lancaster, we were rather surprised when Dave turned up. He headed out while we popped up the ladder and went to Main Line Terminus, to get a bit of boulder hopping practice in.</p>

<p>The way out was smoothly completed, we were miraculously in between showers for the change back at the van in the gathering gloom.</p>
