<h2>Saturday</h2>
<h3>Easegill Country -> Wretched Rabbit via Holbeck Junction: Dave W., Helen, Jarv, Gerardo, Arun</h3>
<p>A beautiful bimble down Easegill; took some of our sexy new blue rope and pulled through all the various climbs. 
Initial descent was smooth, a few scramblings and a bit of help from a rope for the inexperienced + the rust to negotiate Poetic Justice. 
After the climb, there's a short section of crawling before reaching a short pitch; we were backed up for a fair few minutes while Dave 
rigged and the fresher's cautiously descended - enough time to fall asleep in! I rolled over, switched off my light and lay down; 
next moment I open my eyes &amp; its pitch black and I can't hear anybody. Could they have left me behind? Would I have to freeclimb 
County to escape? Thankfully, with almost perfect timing I stumbled down the riftway to the pitchhead as the last fresher dropped. </p>
	
<p>Stomped down the little streamway, over the muddy section &amp; finally popped into Stop Pot after the bedding crawl. Threaded our way 
through Double-Decker passageway and into strawchamber; main stream was very low - and entirely absent in places. Bimbled along Straw 
and revisited a rather familiar spot <a href="/caving/yorkshire/yorkshire-2004-christmas-tour.php#29-arctic">[ahem]</a>. Found a dodgy 1950's era battery under a rock; then stumbled 
along an entirely dry streamway to Holbeck junction, had a sit down halfway along Thackery's passage &amp; a nose up the Pool Sink 
connection and the usually submarine section of stream flowing from there to Double Decker Chamber.</p>
	
<p>Hopped back along to Stop Pot, aborted a trip to Monster Passage in view of the time, and slithered our way out of WR.</p>
	
<p>A great bimble; nice mix of SRT, bolder hopping, hand-line climbing &amp; riftway work - pity I didn't take the camera!</p>

 <p style="text-align:right">Jarvist Frost</p>
			
<h3>Lonkin East -> Rift: Sandeep, Clewin</h3>
<h3>Rift --> Lonkin East: Jan, Shed, Jo</h3>

<h3>Swinstow Pull-Through and Ingleton Waterfalls Canyon: Colm and Andy</h3>

<p>
Saturday was the perfect opportunity to try out ICCC's new take on SRT, with
has recently become the use of literally one rope (often less...).  Colm
also only had a wetsuit, so in conjunction with our Piranas, we could use
this trip as canyoning practice.  The long crawl was almost pleasant, with
Colm trying to get himself as wet as possible.  Although in no rush, we
didn't bother rigging all the pitches, and those that were rigged were made
as sporting as possible.  It's possible to free climb out of Kingsdale main
streamway into Valley entrance so we didn't bother rigging that either.  The
Bradford had actually left a ladder, and a couple of hours later we returned
to sunlight.  Our plan was then do Simpson Pot as a pull-through, but instead
we decided to do the Ingleton Waterfalls Canyon to make the most of the
sunny afternoon.
</p>
<p>
A quick trip back to the NPC to pick up more kit (and via Cafe Anne for a
cheeky cream tea and game of chess) and we were walking up the side of the
River Doe.  This is a stupidly easy trip, although we had to pretend that
everything was scaled up by 10 (including the 1m jumps and 10m pitches!).
Floating on your back down the river in the sun with a fan-club watching and
taking photos is almost pleasant.  Just a pity about the large number of
midges.
</p>
<p>
Met up with the rest of ICCC at the Marton Arms, they'd been down Easegill
(surprise, surprise) and there that conversation ended.  Evening rounded off
with the usual shenanigans at Bull Pot Farm, but luckily I'd already had the
foresight of hanging my furry up in the drying room at the NPC, and washing
the sheep-dip off at the excellent facilities (blatant plug).
</p>
<p style="text-align:right">Andy Jurd</p>

