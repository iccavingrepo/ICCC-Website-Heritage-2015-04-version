<h2>Sunday</h2>
<h3>Simpson Pot -> Valley Entrance: Clewin, Helen, Jo, Shed</h3>
<h3>Swinstow -> Valley Entrance: Jarv, Dave, Jan, Sandeep, Jan</h3>

<p>
<a href="/caving/photo_archive/trips/2005-06-10%20-%20yorkshire/jarvist-IMG_1475.html">
<img align=left src="/caving/photo_archive/trips/2005-06-10%20-%20yorkshire/jarvist-IMG_1475--thumb.jpg">
