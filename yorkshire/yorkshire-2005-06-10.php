A high speed trip to Ingleton to grab a mix of equipment turned into a rather protracted cake &amp; tea fest; bought 40 flatpack batteries 
for the club, a Peli Case for Jarv, Jo a fitting (and therefore childsized) oversuit; and other assorted bits of gear bought out of 
both necessity &amp; luxury.</p>
	
<p>Zoomed off to Valley Entrance, with Dave W. tailing + the Nazi / Comf Meister duo planning to do Rowter. After such tasty flapjack, 
speed was now of the essence if we were to be back in London for a vaguely sensible time. Kit was being pulled on as the Cave Rescue
van trundled along; were we about to become pack mules in a 12hr rescue? Thankfully know; the rescuers were just out and about for a 
sports trip.</p>

<p>		
The valley entrance ladder pitch
had to be rigged before the pull through could begin, so Jarv was thus dispatched down the slithery entrance tube after 2 minibus loads 
of lilliputin primary school kids from Milton Keynes had been swallowed by it.</p>

<p>
<a href="/caving/photo_archive/trips/2005-06-10%20-%20yorkshire/jarvist-IMG_1516.html">
