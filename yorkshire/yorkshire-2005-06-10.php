Overtook the little blighters just before the long wet stoop. Their screams of horror at the cold water chased me along to the ladder
pitch; sounded like I was being chased by a manic herd of cats who hadn't taken their Ritalin. Ladder duly rigged, I pegged it back 
out overtaking the kidies again: "I like your light, Mr Cave Man!", expecting eager ICCCers waiting at the entrance with packed 'sacs 
ready to stomp up the hill as I collapsed out of breath into the grass. But no; this being ICCC - everyone was still eating Creme Eggs 
and discussing the various merits of different MigLight configurations. I love this club :)</p>

<p>
<a href="/caving/photo_archive/trips/2005-06-10%20-%20yorkshire/jarvist-IMG_1476.html">
<img align=left src="/caving/photo_archive/trips/2005-06-10%20-%20yorkshire/jarvist-IMG_1476--thumb.jpg">
</a>
The Swinstow Crawlers bimbled up the hill, located a likely entrance and started the horizontal crawl back in the way that we came, 
except two metres below the ground, on your knees and through water. If ever there was a suitable candidate for a dig...</p>	

<p>
Progress was smooth but sedate; careful fresher-friendly rigging from Dave, gentle pace of SRT &amp; double-checking on the derig to 
ensure safety + that the pull through... well, does!
Slow pace was no problem considering the temperature + relative dryness; plenty of time to practice singing in the wonderful
 acoustics, take photos and eat creme eggs.</p>

<p>Wandering around the corner, we bumped into the Simpson Pot party, loitering at the bottom of the big pitch as shed wriggled his way through the letterbox. Perfect opportunity for a bit of stunt photography!</p>

<p>Time was dragging inevitably on, and our fresher had been left wandering around on the surface - we were beyond the time when we had said we'd expected to be back, so the fear of an accidental rescue call motivated Jan &amp; Jarv to execute a speed exit, knees be damned. Flew along crawls, up the rigged ladder, then zooming through the upper of valley entrance as Jan stayed to rig a ladder belay (well, after I accidentally stole the kit bag with all the rope :).</p>

<p>
<a href="/caving/photo_archive/trips/2005-06-10%20-%20yorkshire/sandeep-IMGP0331.html">
<img align=left src="/caving/photo_archive/trips/2005-06-10%20-%20yorkshire/sandeep-IMGP0331--thumb.jpg">
</a>
Sun was absolutely wonderful, warm and dry enough to stand around while waiting to dry - even as we roared onto the M6 at 8pm, it was warm under the glass of the minibus. Caving in June rocks.</p>

<p>While stunt driving home, Clewin 'I get 100miles to a plate of pasta' Griffith decided to chance getting back from near Reading on a quarter tank of petrol. Within a few miles, the dodgy indicator had gone to 'none' + with a flashing light, the next half hour became a desperate attempt to find a 24hr petrol station before running out of Diesel. Until then, that had been our fastest journey back from yorkshire - not one traffic jam, and a tiny mile-long section of 50mph limit; but at least we got a 67litres fill of petrol (tank is 65 supposedly), at a non-motorway price!</p>
<p style="text-align:right">Jarvist Frost</p>

<h3>Rowten Pot: Colm and Andy</h3>
<p>
Deciding that we'd better run a really easy trip to not intimidate any
current ICCC members, Colm and I offered an easy trip down Rowter (sic) in
preparation for Slov.  Of course the up-take on the offer was zero.  This
means the total distance prussiked by the minibus contingent of ICCC this
AGM was 7 m (unless they put a ladder down valley entrance...)
</p>
<p>
Ignoring the unnecessary half hour it took to drive back from the Farm,
things moved fast with just two of us.  Had not we run out of rope at the
bottom, and Colm's 'minimalist' rigging not caused the rope to get stuck
around a flake, we'd have had enough time to return to the car via Simpson Pot.
We beat as hasty retreat back to Nottingham; we didn't want to have to go
down Simpson to check to see if the others had ended up down the blind
pot...</p>
<p style="text-align:right">Andy Jurd</p>

