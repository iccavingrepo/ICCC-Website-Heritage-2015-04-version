<h2>Sunday</h2>
<h3>Great Douk Cave: Rhys, Tim, Oliver, Fiona, Sam (Andy, Giulio)</h3>
<p>The weather put our plans of a Swinsto/Simpsons pull-through trip to bed so we aborted and went to the Hill Inn instead. Here several cavers, put off by the fearsome reputation of "Dog Cave" as we called it, decided to have a pint instead of 'caving'. Undeterred we intrepid few embarked on an epic mission through the fierce wind to Great Douk. The conditions were so difficult underground that hardcore Italian caver Giulio turned back before the end, dissuaded by the rising water (read: lowering roof). At the end we overcame both giant spiders and giant snow drifts before initiating a fierce snowball assault on ice-axe wielding Andy. Afterwards we enjoyed a battle-earned drink in front of the Wheatsheaf's fire in Ingleton.</p>

<h3>Minibus back to London: Fiona, Dave, Oliver, Giulio</h3>
<p>We conclude that local radio is underappreciated. I don't think I've ever enjoyed the journey back to London so much. Failing to find Folk on Stoke on the airwaves, we instead heard cultural delights like The Green Hills of Tyrol by the Argyll & Sutherland Highlanders on BBC Oxford's Harmony Night. Musical variety is the spice of the long drive home.</p>

