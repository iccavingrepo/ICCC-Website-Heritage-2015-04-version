<p>
  <img src="/caving/yorkshire/yorks0204/meregill.jpg" align="left">
  <h3>Meregill</h3>
  Meregill is a cave I've wanted to do for a long time.  Rumours of
  'dodgy rigging'  and advice to 'bring your rock-climbing gear' had
  put me off.  There was also the matter of a fairly nasty flood 
  warning in Dave Elliot's book. However, with fine weather, the newly
  P-bolted cave was now top of my list
</p>

<p>
  Andy and I headed off after the requisite Bernies stop.  Parking by
  the side of the road, we took the direct route to the entrance which 
  turned out to be fairly easy.  We then spent a bit of time faffing 
  looking for the dry entrance before finding it in the next field!
</p>

<p>
  Bit of a tight entrance tube, but no real problems as we marched down 
  the cave.  Andy and I know each other quite well, so there was no real
  discussion about who rigged what, it just got done by whoever was there.
  The rigging is quite satisfactory, but nothing special.  The final 
  pitche was a bit wet,and I couldn't find a way of fixing the rope away 
  from the wall, so we had to live with it.
</p>

<p>
  The end of the cave goes on and on and on, getting smaller and tighter
  and wetter.  We eventually gave up and set out on our return.  The 
  soaking on the bottom pitch didn't help us on the way out, and we ended
  up plodding, emerging knackered in the darkness.  Back to the car, and
  onwards to the hut for well deserved fodder.
</p>

