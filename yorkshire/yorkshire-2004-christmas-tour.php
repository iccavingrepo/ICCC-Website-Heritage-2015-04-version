<h2><a name="29-arctic"></a>29th Dec - Top-WR + Rescue - A fresher's-eye report from Sarah Arctic</h2>
<p>
Having made a rather late start we trundled off into the mist to Top. I 
geniously tripped on a tuft of grass landing flat on my face (nice spongey 
grass though) and distinctly remember Jarv commenting that this was good 
luck...
</p>
<p>
In we went down the entrance chute, and unbelievably, only steps into the cave, 
water running off the back of the chute made its way down my suit to my 
pants, no way, not at the start! Lots of tight twisty stuff followed, during 
which my FX3 battery ran out (luckily had a second one), and on which I 
continuously got stuck and moaned rather a lot (pathetic fresher). Jo has 
since pointed out that I had all my SRT stuff on my front and should move it 
round to the side next time. Onwards to a 20m descent of a waterfall, 
stopped a bit too much on way down in effort to stay out of water and 
achieved opposite effect - decided momentum is the way forward with these 
things. On the next descent however, forgot to look down to where I was 
going and only remembered to stop about 6 inches from the floor. Everyone 
else zoomed along fine, a bit more twisty stuff and crawling until phew, we 
made it into more open territory. [Limerick Junction - Ed]
</p>
<p>
From here there was increasingly more stepping or climbing over rather big 
holes. Sandeep really didn't seem to appreciate these and would stop and 
look into each one for several minutes as if expecting it to swallow him. We 
were progressing very slowly and poor Jarv had to hoist and drag us up and 
through pretty much everything that appeared slightly difficult - notably a 
climb up over a big hole in a bit of a narrow spot
[Holes in rift passageway above Assembly Hall - Ed]
, I took absolutely ages 
here as I really wasn't getting it. Jarv deftly attached his cow's tail to 
my harness, reassuring, but pretty sure he would have just come down with me 
had I fallen. After much pushing, shoving, squirming and standing on toes, I 
eventually made it up there and across. While waiting for Sandeep to follow 
I got to explore Easter's Grotto - its absolutely amazing, very pleased I 
got to see it, made my day.
</p>
<p>
Onwards through the Assembly Hall, via a 
couple of handrails we had to clamber down, guided always by patient Jarv who 
seemed to literally glide over everything (still can't work out how you good 
caver people do that!). Unfortunately on the handrail climb down into the White Way
Sandeep crashed onto 
Jarv pretty hard, which I think is where Jarv hurt his back. Wading and 
crawling through various streamways ensued, but the stream was rather higher 
than usual, up to my chest in places, and rising. We were having 
difficulties finding the direct way out to Stake Pot or something like that, 
as the river was too high where we needed to go. We searched up and down the 
stream, which was surprisingly strong, I had to yank a weary Sandeep out at 
one point when he got his foot stuck. We climbed in and out of the water, 
Jarv tirelessly searching for alternate exits. The water and the stopping 
and starting however were making us all cold and tired, particularly 
Sandeep, and my teeth were merrily chattering while we were in the water. It 
was also very very late at this point and getting very close to our call 
out, as Sandeep and I had been so slow previously. Backtracking and exiting 
via Top wasn't really an option as it would involve going back over the 
difficulties on the way in, and two very wet SRT climbs. Added to this Jarv's 
back was getting more painful, and us two weren't going to make it out 
without his help. With us all getting cold and tired it was a risk that 
someone would trip and injure themselves or get dragged under in the 
streamway while searching for a way out.
</p>
<p>
Jarv got us to climb up higher into Straw chamber where the rising water 
wouldn't get us (and right on our route - the nearest safe spot to Holbeck Junction) 
and got Sandeep into a survival 
bag. I spent a good 10 minutes trying to wring the water out of my wetsocks 
and emptying my wellies in an effort to get my feet dry for the wait. Of 
course as soon as I put them back on they seemed to have the same amount of 
water in them as before I started. I was cold, but remain impressed at how 
well furries do their job of keeping you warm when theyre wet, it 
definately dried as I sat there and I warmed up. Jarv put gloves and battery 
packs at various points in the vicinity to guide any rescuers to our 
location. Sandeep was pretty hypothermic and pulled his hat right down over 
his face for warmth, was hand-fed a vast amount of chocolate from Jarv's 
food stash (we could have survived there for days with that I'm sure!) and 
was prodded every now and again to prevent him falling asleep. The true 
colours of the ICCC, aka the Imperial Groping Society showed through as a 
pile of slowly-turning-blue people piled on top of shivering Sandeep in a 
kind of human survival bag knot to keep us all warm.
</p>
<p>
Several hours of nattering away, and a Christmas pudding later, we finally 
heard real voices over the ghostly echoes of the streamway, and Simon from 
Oxford appeared with Mark. Jarv crunched a few pain killers and got going 
with Mark, and I was very eager to go myself as I just wanted to get moving 
off and out and warm again, but Simon wanted us to wait to move till CRO 
came, and made us breathe a thing called a Dragon in the meantime (hot air 
that kinda warms you from the inside). An hour or so later (not quite sure) 
they turned up and force-fed us more chocolate (feeling way over-sugared at 
this stage). Sandeep now seemed a bit stronger and went straight off with 
them, but I somehow needed the toilet yet again, and this coupled with 
difficulties getting back into my harness (probably all the chocolate!) 
meant that the cavers with Sandeep got ahead of us, and unfortunately didn't 
wait. It soon transpired that my CRO guys didn't know the system and had no 
idea how to get out - and so I returned to the now rather too familiar 
process of stopping while they check out the route, move a bit, oh no, turn 
back, move a bit oh we'll just go check this route then that route etc. My 
thoughts were 'you are supposed to be rescuing me!' To make things all the 
better I slipped off a rock and really thwacked my knee which was flippin 
painful for all the crawling later. Then my second battery died, and as 
moving without a light was quite difficult someone leant me a tikka, which 
was better, if a little dim.
</p>
<p>
