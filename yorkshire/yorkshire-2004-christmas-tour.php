<h2><a href="27-sandeep"></a>27th Dec - Night Trip down Bullpot of the Witches</h2>
<h3>2hrs: Jarvist [Leader], Sarah, Jo and Sandeep</h3>
<p>
After an extremely large amount of faffing to find 4 sets of club caving 
kit, we headed off to Bullpot of the Witches; notcing that a very large 
and overflowing stream was heading the same way along the path. Walk down 
to the cave entrance was past a memorial plaque.</p>
<p>Once in the cave, there was a fairly tight climb down. Jo got down to 
the bottom with no trouble; Sarah followed but it proved a rather 
challenging first-ever-climb. She aborted + came back to the top. Sandeep 
made his way down with a little bit of difficulty; followed by the 
refreshed Sarah. Jarv dropped in with a controlled fall rather than climbing.</p>
<p>Short climb onwards to the main chamber was easily negotiated by all; 
leading along some easy passageway to the massive open hole + enormous waterfall.
Way on down a climb in the waterway was blocked by the enormous volume of 
water flowing through. Any evidence of dead sheep would have long since 
washed away. Everyone exited with no trouble and returned to 
Bull Pot Farm to drink copious amounts of tea.
</p>
<p style="text-align:right">Sandeep Mavadia</p>

