<h2><a name="26-jarvist"></a>26th Dec - Cow to Manchester Bypass</h2>
<h3>5hrs: Tetley, Jarvist + Gavin [OUCC]</h3>
<p>
<a href="/caving/photo_archive/tours/2004%20-%20christmas%20in%20yorkshire/2004-12-26%20-%20easegill%20cow%20to%20county%20-%20jarvist/IMG_0087.html">
<img align=right src="/caving/photo_archive/tours/2004%20-%20christmas%20in%20yorkshire/2004-12-26%20-%20easegill%20cow%20to%20county%20-%20jarvist/IMG_0087--thumb.jpg">
</a>
Open-air pot rigged by Jarvist + left to freeze over the next few days. 
Played around in old-workings before finding modern clamber along slow 
flowing streamway + arriving at traverse above Fall pot.</p>
<p>Tetley rigged from the many ceiling P-bolts; requiring go-go-gadget-legs. 
Gavin rigged the big pitch; with a rather disturbing flake / rub point that 
had to be carefully avoided on the swing to the freehanging rebelay. Planning 
for a trip with the less experienced down here, Tetley added a deviation to 
prevent Death of Fresher [DOF].</p>
<p>Dropped through boulder choke almost directly by where rope landed. Drippy 
climb down onto streamway; sporting upstream bimble with some waist deep pools 
to be sidled around. Easily visible [from streamway] roped climb to left; 
followed up into the dry high route. Initially went the wrong way; realised + 
followed compass [SE] along enormous boulder-strewn passageway. Old lags 
absolutely zoomed aong; exhausting work keeping up.</p>
<p>Minorette's [enetered via tight climb-down on right at end of large passageway] 
were amazing - 3 parallel and very pretty passageways connected by unlikely 
+ very short tranverse crawls.</p>
<p>Came out in more large passageway; with fairly tedious boulders to clamber 
over. Ate cognac-laced Christmas pudding at Terminal Junction - have been won around 
by Clew's controversial choice of cave food!</p>
<p>Left SRT kit in pile + dashed across to Holbeck junction via Stake pot, 
in order to connect-up internal map. Met 2-guys on a mission of surveying by the 
start of the crawl to WR from Stake Pot. Popped back up the ladder to the T-junction 
and started on the depraved Manchester Bypass.</p>
<p>Horrid flat crawl led up to right into pretty chamber + then immediate down on 
left to slightly more open crawl - but which had a central rift that one had to continually 
lift oneself out of.</p>
<p>Fantastically quaint 'fixed' rope ladder, with 1-inch chain sides and welded rungs 
18 inches or so. There's a real fixed ladder sitting in Bullpot waiting to replace it 
- for shame!</p>
<p>3m climb up sheer wall to wander round to right to 'freeclimeable' [supposedly; 
I'd only do it when really really desperate] 5m pitch in County. Climb out county 
along tight trickling streamway + an easy entrance Chimney.</p>
<p>Easy wander back across Moor - the bog is so much more pleasent when its iced over!</p>
<p>Pints of Tea + Pasta Bakes for Veggies + Meat Murderers alike.</p>
<p style="text-align:right">Jarvist Frost</p>

