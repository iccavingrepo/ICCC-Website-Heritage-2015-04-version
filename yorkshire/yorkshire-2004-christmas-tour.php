Arrived Charing Cross 10pm on the 24th Dec, and stomped down to Pimlico looking
like a suicide bomber with rucksacks fore and aft. Tetley and Martin weren't in 
the pub [it was closed], so dropped off stuff at Tet's and went to an open pub instead.
