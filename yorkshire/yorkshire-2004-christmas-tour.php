</a>

<p>Drove to Yorkshire spatchcocked by a random assortment of daybags + water 
bottles, rear right mudflap was scraping along the ground until we got up to 
speed on the Motorway. Christmas lunch was a mid M6 penknife prepared Brie + 
Bread delicacy.</p>
<p>Bullpot was true cliched Christmas-Card material; car overheating as we 
carefully drove up in 1st. Snow everywhere; unpacked + put Duck + NutRoast in oven 
and left the capable Martin in charge.</p>
