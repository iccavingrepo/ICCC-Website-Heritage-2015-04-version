</a>
Eventually we came across a CRO guy who knew the way out, and from there on 
it was plain sailing. I soon warmed up when we were moving and we even met 
another CRO guy after a while who had a brighter headtorch for me. No-one 
seemed able to remember my name though, one guy kept calling me Sally and 
the other calling me Clare. With no SRT kit on I slipped round the twisty 
bits like a snake and with some pushes and shoves we were at the main 
climb in Wretched 
Rabbit, which had a rather handy ladder running up it, so all I had to do 
was put my hand on the ladder and feet on the rocks and I was away. The 
crawl up to the surface was a lot shorter than I expected so I was really 
quite surprised to meet probably about 20 people very rapidly after 
finishing the climb. I was absolutely sweltering at this point from all the 
moving! Lyndon handed me a nice cup of tea, and took a photo (not kind you 
know, not kind). I really didn't believe him when he said it was 6am! 
Anyways we trundled back over the moor to Bullpot, where there were yet more 
CRO people congregating, I said thanks to as many as possible, and then 
dived in the hut for a lovely hot shower, various nice people locating me 
towels and tea and clothes. Everyone else was back before me, tired but 
fine. All were heading for bed but I was kinda past that stage so I faffed 
for ages trying to make myself more tired. Climbing into sleeping bag had 
bizzare experience of seeing cave walls when I opened my eyes in darkness.
</p>
<p>
Many people have asked if this has put me off caving. Nah. I just happened 
to be on a trip where luck wasn't on our side and a chain of things went 
wrong. The decisions made kept us safe and I appreciate that. I also now 
know that there is a cave rescue and it is very comprehensive, I was 
astounded at the extent people went to help cavers when something went 
wrong. Of course I hope never to have to meet them again, but its nice to 
know they are there.
</p>
<p>
I haven't really done enough of caving to learn what it is about, but I like 
the fact that it is always a challenge. I think being rescued magnified the 
fact that being underground is very thought provoking. In my brief 
experience I found that there is something about caving which brings out the 
life in you, away from the everyday world's fakeness. Sorry, I imagine that 
sounds totally ridiculous. Basically now I just want to take more 
challenges, to improve and get stronger. Anyways many thanks to Jarv, Simon, 
all at Bullpot and the CRO.
</p>
