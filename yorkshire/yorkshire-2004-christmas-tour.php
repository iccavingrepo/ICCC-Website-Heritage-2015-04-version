squeezed into every available nook and cranny - violating just a few laws 
of physics in the process.</p>
<a href="/caving/photo_archive/tours/2004%20-%20christmas%20in%20yorkshire/2004-12-26%20-%20easegill%20cow%20to%20county%20-%20jarvist/IMG_0083.html">
