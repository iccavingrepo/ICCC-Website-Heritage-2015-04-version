After breakfast the creeping tendrils of apathy were working their evil magic, and I had to escape the fester of cavers. Safely upstairs I found strength in '<i>Northern Caves Three</i>'; and a description of Aygill caverns. When the announcement came, that a party was going to a cake shop in Ingleton, I had the strength to resist the mob and gather a crew of eager freshers.
</p>

<h3>Aygill:- 
Jan (fearless leader), Jack (not doolittle), Alvin (2nd in command and 1/3 of the triads), Lester (2/3 of the triads), Nicholas (3/3)
</h3>

<p>This is a great little cave, and good for beginners, some crawling, some squeezing, 2 pitches and an impressive streamway. and the trip would have been a lot smoother if hadn't put in a deviation from hell on the second pitch, there was some struggling on the way out, with back prussiking and change overs. executed very well, given the inexperience. and once again i failed to pass the cascades at a very commiting waterfall climb. 
</p>

