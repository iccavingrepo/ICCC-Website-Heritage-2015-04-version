Treading familiar ground [we were here 3 weeks previously]; we compensated for our late start by flinging ourselves efficiently into the cave system. There was more than a little nip in the air; where I had previously walked with unzipped furry + oversuit arms tied around my waist, I was now fully suited up gloves 'n' all.</p>

<p>Entrance ladder was decidedly more drippy; oddly the streamway was comparitively subdued. Navigation to the first pitch head was quick, Lester seemed rather bemused by the constant squeeze - Jo barely noticed. Decided to adapt a controversial top-of-pitch positioning for the descent of the freshers, deciding that my efforts would be better spend making sure that Descenders were rigged correctly than providing an emergency cushion.</p>

<p>Jo went first down Penknife Pitch [with instructions on how to reverse belay]; steady descent deftly avoiding the brunt of the waterfall. Lester followed at a rather breakneck pace - temporarily worried that he was about to execute a fresher-plummit, he seemed happily in control and completed the abseil faster than I! I have a sneaky feeling he might have done this before while in the Singaporian army...</p>

<p>The Second pitch was completed with similar ease, so we did our best to get lost in the rather confusing section of streamway + oxbow beyond. Quite labyrinthian; would start to get rather worried about ever getting out - were it not for the many little red reflectors dotted along the exit route. Unfortunately very little help when trying to get into the cave - but perhaps thats for the best!</p>

<p>Jo saw the way on round the back of a boulder in the passageway where the streamway drains noisily through a hole in the ground. We quickly tripped our way along the fast flowing streamway down to Leprecaun junction - easy going downhill, but the 1.5m drops can require a helping hand on the way out - the smooth rock makes it difficult to chimney.</p>

<p>Resting momentarily to eat a cereal bar at the far-too-familar Leprecaun junction [we had spent about an hour searching the 8 odd exits], we then executed the upwards left-spiral climb. Long-legged Lester easily bridged his way across, Jo was at rather a disadvantage. Not particularly liking the muddy traverse if she went up the easier climb on the far-side and being pushed for time for our meetup, I decided to try a spot of 'fresher throwing'.</p>

<p>Way on to Nagasaki chambers was easy; whereupon we heard voices + saw the flash of headlights. Turned out to be a couple of rather terse cavers doing some drilling up in the roof. They pointed the way on _through_ the large pile of rocks that divide the chamber; I think I actually felt safer climbing right over it last time.</p>

<p>Large holes that fall into the assembly room [location of the Jabberwocky fight last time] provided entertainment as ever. Coupled to a fresher with cow-tails, I'm not sure whether I'd be able to stop them if they did properly slip through the hole. Quite romantic though; a plummit with just enough time to admonish "FOR THE LAST TIME; MAKE SURE YOU JAM YOUR ELBOWS IN!".</p>

<p>Had a quick lie-down in the East Grotto and admired the stal + our snicker bars. The two handline climbs were smoothly conquerered - though I swear that the second handline actually makes the climb more difficult!</p>

<p>Once in the WhiteWay, we quickly followed it along to its low-termination, then backed up very slightly to find the '<i>obvious way on</i>'. Not sure how we managed to miss it last time; its on the left about 10m back from where the stream becomes uncomfortably low.</p>

<p>Followed the dry but rather smelly easy-going passageway to the meetup spot [Haldane junction?]. Heard voices - too many voices, and emerged to find both the Wretched Rabbit crew + our other halves.</p>

<p>
<a href="/caving/photo_archive/trips/2004-11-26%20-%20yorkshire%20-%20jan/Photo21.holbeckgroup.janevetts.html">
<img align=right src="/caving/photo_archive/trips/2004-11-26%20-%20yorkshire%20-%20jan/Photo21.holbeckgroup.janevetts--thumb.jpg">
</a>
Jan entertained himself by flashing us a few times, as is his persistent want when underground. Swapped a few navigation tips + stole all of Clew's krabs to assist in the derig and began our ascent.</p>

<p>Was quite worried about navigation out of Pool - map looks distinctly crinkly both before you get to Jacob's Ladder [first pitch] and after you get off the last pitch. Nothing to worry about really; keep on the left on the way out to get to Jacob's ladder - and after the last pitch simply follow the way that looks vaguely possible for human-sized cavers.</p>

<p>Jacob's ladder was incredibly noisy; and rigged exceptionally wet. Due to someone [ahem] not packing enough Maillons, we were rigging with a minimal amount of metalwork - nowhere near enough to support the long P-bolt traverse out of the Waterfall. Oh well. Spent a good few minutes shouting over the noise of the spray, putting on hoods, tightening cuffs and checking SRT kit.</p>

<p>As ever, I stood at the bottom pulling rope through + trying to keep the freshers as dry as possible. Which is fine and exactly what Caving is all about - the more experienced sacrificing a little comfort to save the less experienced a lot worse. However, Lester seemed absolutely determined + rather skilled at kicking me in the face for as long as he was in range. If I ducked his prussicking foot, his other would come wheeling around to clobber me on the side; if he missed with the standing stroke, he'd manage to get a heel in my teeth as he lifted up his hand jammer. Very surreal, but it did pass the time.</p>

<p>Last bit of passageway involves a 2m freeclimb into a fairly tight crawl-space. Secret of getting up was to gain sufficient height in the wider chamber before trying to enter the much tighter v-shaped beginning of the crawlway.</p>

<p>Adopted normal tackle-bag dragging + person behind freeing technique. Would have been very frustrating on the corners without someone behind to unhook the anchor. Last dog-leg just before the entrance was horrendous for the long-femur'ed; felt convinced when bending backwards that I was about to kick myself in the back of the head.</p>

<p>Escaped into the dark, soft rain was quite refreshing. Navigation in the valley was fine, but once up on the moors you hit the fog. Vis within the fog banks was 4-10m, no chance of finding the post-markers back to bullpot. Walked a bearing of 330 degrees from the Stile, passing over some distressing boggy land - amusing to watch Jo suddenly disappear up to her waist with a far-too-happy squel, considering the very serious risk of exposure if we got properly lost. Walked back + forwards along stone wall until we found the trailer marking the road to Bullpot. For future reference, on a 330 bearing we went _over_ a slight hill, trailer was about 400m to the LEFT which was also mostly downhill.</p>

<p>Only spent 90-odd minutes getting back, not bad considering we took 60 to cross the moors in the daylight - but it was pretty grim at the time! In between fogbanks, we could see lights - but was that Bullpot or the farm miles across the Valley? Will o' the Wisp's to lead we poor cavers to our boggy doom!</p>

<p style="text-align:right">
Jarvist Frost
</p>

<p>After consuming nearly a gallon of pasta sauce, we roasted chestnuts by the-modern-equivalent-of an open fire ie. an oven, and discovered the decadent delights of baked bananas and nutella.
</p>

