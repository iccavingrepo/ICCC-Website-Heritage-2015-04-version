<h3>Top to Pool crossover:-
<br>In Top out Pool: Jarvist [would you follow this guy?], Jo, Lester
<br>In Pool out Top: Clewin, Alvin, Charles</h3>

<p>
<a href="/caving/photo_archive/trips/2004-11-26%20-%20yorkshire%20-%20jarvist/IMG_0666.html">
<img align=left src="/caving/photo_archive/trips/2004-11-26%20-%20yorkshire%20-%20jarvist/IMG_0666--thumb.jpg">
