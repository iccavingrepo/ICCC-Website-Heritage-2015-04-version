<p>
  It was a woman's cave m'lord.  Descended at about 5pm, par for the course for an IC<sup>3</sup>
  trip.  After the entrance pitches, we split into two groups.  First Mate and Sea-dog took the
  woman's route, while Red Beard set off alone.  Fine rigging, great traverses in a 
  vertical maze led us to the bottom, where the two routes converge.
  Andy rigged a fine roof traverse which led to two more pitches, and the sump.  Arses were dipped,
  and we started on our way out - a race against time to reach the Marton before 11.  Being the 
  solo caver, I completed my route first, and exited just as the sun was setting.  First Mate and 
  Sea-dog were only just behind, as we raced back to the car.  Changed in record time, and into the
  Marton by 5 to 11, to discover that Black Sheep was off!  Still, Timothy Taylor was a good second
  choice.<br>
  Usual stodge for dinner, couple of pivos then bed.  Quick knackered.
</p>

  <h3>Canyoning the Waterfalls Walk</h3>
<p>
Another fine morning without a cloud in the sky, though a slightly more relaxed start meant
  we weren't in Ingleton 'til after 1pm.  The water levels were low, as it hadn't rained in 
  quite a while, but there was still plenty for a good trip.  An excellent toboggan starts the route
  or a massive 10m jump for the more brave.  Pitches, jumps and swims, all concentrated in a short
  route make this a great easy canyon, especially on a sunny Sunday when it becomes a popular
  spectator sport!  Unfortunately, our rope was a bit too short for the final pitch, but Andy 
  volunteered to climb back up for it (2x15m ropes ideal for the canyon).  Great fun, and a trip 
  I'm definitely going to do again.
</p>

