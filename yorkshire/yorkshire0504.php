<h1>Yorkshire</h1>
<h2>21-23 May 2004</h2>

<p>
  What a weekend!<br>
  A Saturday morning warm-up in Simpson Pot, followed by a classic trip. 
  All rounded off nicely by canyoning the Ingleton waterfalls walk in perfect
  sunshine.  Does it get any better than this?
</p>

  <h3>Simpson pull-through</h3>
<p>
Unbelievable, we were awake, breakfast eaten and out of the NPC before
  9:30.  Stopped at Bernies to make sure the through-trip was doable, then off to Kingsdale.
  We were underground well before 11, soon clambering down the easy climbs in the entrance.
  The point of no return was soon to come, with a small 5m pitch.  The duck was as cold as 
  ever, though this may have been because we were wallowing around posing for photos.  The 
  inevitable 'stuck rope' debacle was easily sorted with a quick prussik, and we soon arrived
  at slit pot.  Even anorexic Andy struggled through the slit, so I didn' feel so bad when it 
  took me several attempts.  Bit of crawling in the bottom leads to valley entrance <i>via</i> 
  a fine bit of stream passage.  Out in the sunshine by about 1, and back to the NPC for tea, 
  apple-pie and cream.
</p>

