<p>A short trip, bit damp, couple of pitches with some tricky deviations.</p>
<p style="text-align:right">J Evetts</p>

<p>You might think that because there were just 5 of us that we would be out early but you'd be wrong. The hardcore of ICCC faffers made it out at 2pm after having got up at 10am (all the other cavers apart from two had already left). During the intervening time it appeared that we'd drunk about 6 pots of tea and talked endlessly about which cave we to do as well as carbide and Miglights, again. After having dropped Jan off at Inglesport cafe to further keep up good name of ICCC procrastination, the rest of us headed off to Hardrawkin for a nice bit of SRT. This was the plan so Clewin and Chris didn't bother putting on any kneepads. We got to the entrance and while we were getting ready to go in a group of kids came over ogled Clewin's carbide, immediately after I reminded him that carbide is mad.
</p>

<p>A quick climb down at the entrance followed by some walking past some brilliant white popcorn formations and a little wet crawling and we were at the first pitch. I offered to put my newly learnt rigging skills to the test but apparently it's quite hard to see where the deviations go and after watching Dave pendulum across the pitch, with increasing energy each time, I was glad I didn't go down first. I heard "Rope Free" and headed down. The first deviation wasn't the easiest one in the world but passed after a little grunting and groaning. When I was down I shouted "Rope free" and headed off with Dave to the next pitch. The climb down immediately after the first pitch looks somewhat daunting at first but once you have a go it's fine. The rigging guide suggests two mediumish pitches but the first section of the second pitch can be done as a climb followed only by a short straight pitch to the bottom. Once you reach the bottom make sure you step the right way because on one side there is a 12m deep sump and the other is a nice rock to sit on.
</p>

<p>Everyone arrived at the bottom but we decided not to hang about and Chris was given the job of derigging. When everyone apart from Chris was at the top of the first pitch we decided to start moving slowly out to avoid getting cold. Although Clewin didn't particularly enjoy the crawls he enjoyed them even less when I pointed out how wonderful the new Jarv [<i>Ed: Ahem! Idea stolen from Hilary Greeves</i>] patented double thickness kneepads were. I saw what looked liked some lichen on the wall but they need light to grow so I was a bit confused by that one. Clewin and I waited just inside the entrance and snacked on a Snickers bar until Chris arrived with a packed tackle sack. We were changed and off by fiveish.
</p>

<p>We hit the hut for an uncharacteristically speedy clear up; bags were packed, cheese sandwiches prepared, ropes sorted and Jan part-exchanged his old Miglight for the "final" prototype. We were in the car for 18:30 and in stores for 23:15. During the drive back it took Jan an hour of fiddling to get his new Miglight set to the modes he wanted.
</p>
<p style="text-align:right">Sandeep Mavadia</p>
