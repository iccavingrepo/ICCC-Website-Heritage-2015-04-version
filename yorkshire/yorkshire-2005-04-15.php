<h2>Saturday</h2>
<h3>Providence pot: Jan, Clewin, Chris</h3>

<p>traverse traverse traverse</p>
 
<p>rattle rattle splash</p>

<p>"uh... it's about waste deep"</p>

<p style="text-align:right">
Chris Rogers
</p>

<p>Rain had stopped, but as we drove up Wharfedale there seemed to be numerous springs coming out the hills, was this normal?
</p>

<p>We drove through Kettlewell and carried on up the valley to find where the carpark was for Dow cave. We met some local cavers, parked just at the bottom of the steep hill, they were going to Dow Cave, but reckoned the exit for Dowbergill passage into Dow Cave would be sumped. We decided to investigate the Providence end.
Back in Kettlewell we turned left and parked before a bridge, in front of several quaint houses. We proceeded to strip and change into gear, at the best we thought it might spice up the neighbours weekend. Unsurprisingly a local man soon came out and we were moved, he was friendly enough though and advising us to not bother with Prov as the entrance would be underwater and he didn't want to read about us in the news.
We thanked him and said we would just have a look.
</p>

<p>Following the convenient signposts, we walked the pleasant half-hour upto the obvious manhole covered entrance, placed in the middle of the stream. Clewin did the honourable thing and went first, a clamber down scaffold lead to a very drippy riggle through a hole, we were off.
</p>

<p>The description was useful to find the Blasted Crawl, but we soon figured that the wire more or less lead you the right way. The Blasted Crawl was a joyful wallow/crawl in 8inches of gloop, with a bit thrutching for good measure. We popped into the mainstream at Stalagmite Corner and headed down stream, this was the start of the renowned Dowbergill passage, dead straight for a kilometre or so but deceptively time consuming apparently.
We weren't in the mood for freediving, so we just decided to go for an hour or so.
</p>

<p>It was really nice passageway and pleasantly spacious, with the occasional scramble over boulders, we just followed our noses. We reached Bridge Chamber, just a wider bit, and passed under the hanging death that gives the place its name. Then it was down into a bit of rift, narrow but with a deep stream, some dedicating themselves to staying dry by traversing the whole way, almost...
</p>

<p>We soon found ourselves climbing up a fair bit following some vague arrows, then back down into the stream; it might have been a taste of some of the trickier route finding further on. There didn't seem to be much sign of the halfway point, 800 Yard chamber, so we decided to leave it for another day and got the bullet train out of there.
</p>

<p>Clambering out the entrance, some kind passerby had built a snowman for us and the sun was shining in all its glory - but it still felt like bloody Greenland.
</p>
<p style="text-align:right">J Evetts</p>

