<h2>Saturday</h2>
<h3>Easegill Lancaster --> County; Jo, Clewin, Jan &amp; James</h3>
<p>
Clewin, Jan, Jo and James - Originally Jo and James's fate was going to be
left in my hands, but Dave W was deeply suspicious of my route-finding,
and rightly so! For even with the extra guidance of Clewin-the-wise I
managed to fit in some interesting sight-seeing...
<br><br>
Particularly hazy in my mind was the section from the iron ladder, at Stop
Pot, to the County Streamway; failing to find the way downstream through
the boulders below Stop Pot to County, we opted for the Four Ways chamber
route. All went swimmingly, but fatigue was soon to be our undoing as we
neared the final pitch in County - we were foiled by an obvious way on
that lead to a not so obvious way on.
<br><br>
A while later, after bashing through some really rather sinuous rift, we
back-tracked to the stream and found the proper way on, and finally, five
and half hours later, the exit (but not before I bravely volunteered
Clewin to do some free-climbing!).</p>
