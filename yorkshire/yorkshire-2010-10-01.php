<h3>Myles, Kate, Clare, Al, DaveW, PeteH, Jim, William, Jarv, Sandeep, Nico, Shed, Jan, Goaty</h3>

<p>With term, and all the new cavers, fast approaching, we managed to squeeze in a quick little experienced trip to Yorkshire to get some non-novice caving trips in.</p>

<h2>Saturday</h2>

<p>Heavy rain across the UK on Friday dictated the last-minute change in
plan and abandoned of our Magnetometer + Masongill spectacular
designs, settling for some rather more weather-proof classics... As it
turned out, it was actually really quite dry below ground - the flood
pulse must have been long gone.</p>

<h3>Bullpot: Nico (Rigger Extraordinaire), Jarv, Al</h3>

<h3>Jingling (Lateral Route): Kate (Rigger Extraordinaire), DaveW, PeteH</h3>

<p>Ah, Bullpot - fast becoming my favourite place to introduce cavers to
the delights of rigging. Entrance next to the little tree behind the
scar quickly located, Nico was tasked with all the rigging. Entrance
via the usual deviated dangle (not the new y-hang bolts), 2nd pitch as
per usual, followed by the 'slot' 3rd pitch. Traverse quickly made to
the fine 4th pitch, dropped via a y-hang with two deviations. The
lower of these, off the easier to swing rawl bolt was awfully loose
(does no one pay attention when rigging anymore?), so spannered it up.</p>

<p>A brief little look at the lovely confluence and bit of stream above
the final pitch (never seen the point to descend it...), and a speedy
exit, leaving the cave rigged.</p>

<p>We met the Jingling crew literally at the gate between the fields, and
quickly followed their ropes down the lateral route &mdash; very nice!
Nico took the derigging mantle, with Al being his second / bag
carrier. Exit to dusk, gentle walk down to the waiting minibus and a
partially changed Jingling party (justifying their speed at the
clearly superior skills of their rigger / derigeour).</p>

<p style="text-align:right">Jarv</p>

<h3>King Pot: Tetley, Clare, Jim, Myles</h3>

<p>All I know is that they set off shortly after 1pm, and returned, just
before their callout, to the road of Kingsdale at ten to midnight. Mad
staring eyes, tales of epic tackle bag dragging by those who foolishly
haven't been caving since last term, and the beauty and horror of
standing at the bottom of Elizabeth pitch contemplating the return
journey...</p>

<p>Returned to the NPC to find cavers snoozing in front of the fire, and
a truly epic chilli created under Shed as head chef, and truly
debauched quantities of treacle tart with lashings of double cream
thanks to Jan.</p>

<p style="text-align:right">Jarv</p>

<h3>Marble Steps: Sandeep, Shed, Jan, Goaty</h3>

<p>Believe they had a fairly successful trip, but were feeling rather sore after being cheated from the prize of Large Pot by the weather.</p>

<h2>Sunday</h2>

<p>Awoke to torrential rain. This quickly drowned any motivation to go
caving, and so we settled into a day of fettling, tea-shop caving, and
gear overhaul.</p>

<p>Packing to go home we had a packed lunch composed mainly of treacle tart &mdash; but what to put the left over double cream in? Surely the plastic pot would explode in the minibus rather distastefully. Jan had the solution &mdash; an empty beer bottle with the cap pressed back on, perfect for chugging from while gently drifting home back down the M6.</p>

<p style="text-align:right">Jarv</p>

