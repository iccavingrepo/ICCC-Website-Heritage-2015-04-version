<p>
  Early start! We went down at 3pm, the top of Walrus pitch was already rigged (yet again a pull-through foiled by fixed rigging<sup>1</sup>). From  Limerick Junction the route is best described as - <i>follow the stream, climb up when it looks like there is a higher level traverse and then you are on course for the Bridge of Sighs</i> - we started with following the guide description, but it was about as useful as a chocolate teapot.</p>

