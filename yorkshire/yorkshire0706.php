<p>
  From Nagasaki chamber its easy going and with a few diversions we we're soon at the Pool Sink turning off the main streamway. Our description failed to mention turning right beforehand for Holbeck Junction, so we spent a bit of time being lost and thinking of the beer awaiting us.</p>

<p>
 Holbeck junction takes you via huge passage to Stop Pot and the start of WR, through Four Ways Chamber. From here we skipped merrily out of the cave. Outside the beer was delicious, but midges soon descended for a feast of smelly caver so the moment didn't last long.
</p>

