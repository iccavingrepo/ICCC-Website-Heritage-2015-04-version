<a name="sun"></a><h2>Sunday 16th: Rat Hole -- Stream (Gaping Gill) Exchange</h2>
<h3>Depth in the Afternoon (Rat Hole)</h3>
Nb: Written for Felix, our student newspaper, to encourage new members and so is more dramatic than the reality!
I didn't use the pendulum rope as I was unsure of the belay: its apparently as safe as houses.
<p>
<a href="/caving/photo_archive/tours/2006%20-%20yorkshire%20winter/IMGP0694.html">
<img align=left src="/caving/photo_archive/tours/2006%20-%20yorkshire%20winter/IMGP0694--thumb.jpg">
</a>
We stagger our way up the path weighed down with bags of rope and metalwork, passing the toddlers being carried to see Father Christmas at the Ingleborough Cave, our comedy yellow PVC suits cracking in the sub zero temperature. The sky is a perfect blue from horizon to horizon, the bog frozen beneath our feet.</p>

<p>It was the first caving day of our Winter Tour and this was our plan: To split into two groups and rig separate ways down into the enormous Gaping Gill cave system, meeting in the main chamber and swapping routes - going out on each other's ropes. The others set out across the moor on a compass bearing, while we hop over a fence to protect the unwary from the stunning 110m waterfall.</p>

<p>A last recheck of our gear, a few nervous grins, and we dive in. We start by crawling down an old dry entrance, a distant roar becoming a thud pounding through the limestone as we approach the underground stream. We regroup perched in the middle of this torrent and I attach our bags with 150m of rope between them to my harness.</p>

<p>A few short pieces and soon we approach the monster. Swinging from ceiling bolt to ceiling bolt, judging lengths and tying rope as I go. Finally out above the main drop, below is an utter maelstrom. The  river pours over a jagged lip and unravels into disordered streams, ricocheting off the walls as it plunges down. To go straight down would be suicide - the hurtling water would first batter and freeze and then drown you. Through the spray is a view of pure blackness - the ninety odd metre drop to the floor.</p>

<p>And so our way is to traverse, abseiling down a little, carefully holding steady and then throwing yourself across the shaft towards where you hope the next bolt will be, bouncing off the walls and scrambling along with whatever holds you can grab. With the rope safely tied off, I kneel against the wall and contemplate the next obstacle - a traverse  between two overhanging walls a little over two metres apart, with tiny footholds, to gain the next bolt and place of safety. There is a crack of a ledge on one side, and a few graspable bumps on the other. The climbing wall does not prepare you for moments like this.</p>

<p>I pull the necessary slack through my descender - no chance of being able to self-belay as I climb, and set off - bridged across from one wall on tip toes standing in the crack on one side whilst pushing off fingertips on the other. I don't remember what moves I used to get across, or how I stepped from one side to the other when the ledges ran out. My only memory is being in the middle and looking down, feeling the strain in my tendons, watching the drips plunge to the floor.</p>

<p>On and drifting down on a 9mm spindle of rope, lassoing the walls to swing in to the bolts finally landing on 'The Amphitheater', a scallop shaped ledge with the waterfall-powered winds whistling past. One hundred metres of rope used so far.  The view from here is amazing - looking down into the enormous main chamber. Downing my light, I can just make out the daylight filtering down from above, so dim and speckled from bouncing off the rocks that it could as well have been the Northern lights.</p>

<p>
<a href="/caving/photo_archive/trips/2007-05-25%20-%20Yorkshire%20JSPDT/jarvist%20frost%20-%20easegill%20-%20main_drain.html">
<img align=left src="/caving/photo_archive/trips/2007-05-25%20-%20Yorkshire%20JSPDT/jarvist%20frost%20-%20easegill%20-%20main_drain--thumb.jpg">
