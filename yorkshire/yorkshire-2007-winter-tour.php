I set off again, knots tied to bolts, karabiners clicking in to hold the soft rope away from the jagged rock. The walls blossom out and I am left in free space, dangling between two waterfalls. The weave of the rope sets me gently spinning as I drop to the floor, my descender purring to itself as my height is turned into puffs of steam freed from the wet rope.</p>

<p>Down on the cobbled floor, I dodge the other seven waterfalls as I stumble across a chamber big enough to happily host the Albert Hall, blowing my whistle till I am out of breath to signal the others. Eventually (after a lot of blowing!) they get the message and a pinprick of light appears a long way up above and starts to drift down as slowly as a feather.</p>

<p>With perfect timing, the other party arrives. We share a wee dram and a sip of hot chocolate, and make our separate ways out. Passages and chambers and climbs and rope: finally we were back on the surface, nearly eight hours underground.
Starlight glinted from the crystal clear sky, lighting the white beards of frost on the heather. To think a day ago I had been walking home under the orange glow of South Kensington! We walked the long path back to the minibus, the ropes dangling from our belts stiffening into rods as they froze solid.</p>

