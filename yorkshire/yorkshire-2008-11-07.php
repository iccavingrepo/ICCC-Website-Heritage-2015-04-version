<h2>Saturday: Gaping Gill (Flood to + fro Bar)</h2>
<h3>Jarv, Dan, Rik, Gergely, Edmund, Alex, William, Dave W, Tim, Carrie, Kev</h3>
<p>
<a href="/caving/photo_archive/trips/2008-11-07%20-%20yorkshire/jarvist%20frost%20-%20canon%20g5%20-%20gaping%20gill%20-%20getting%20changed.html">
<img align=right src="/caving/photo_archive/trips/2008-11-07%20-%20yorkshire/jarvist%20frost%20-%20canon%20g5%20-%20gaping%20gill%20-%20getting%20changed--thumb.jpg">
</a>
Zoomed up to Clapham and found a suitably picturesque piece of Autumnal lane to abandon the minibus in. The long slog up to Gaping was... long and slogful. We had a permit, perhaps for the first time, which made us feel a lot less worse at getting in the way of the pirates.</p>

<p>Bar was naturally far faster to rig, and so we waited in the dark of SE passage for a good while until Dave appeared above abseiling into the pot.</p>

<p><a href="/caving/photo_archive/trips/2008-11-07%20-%20yorkshire/jarvist%20frost%20-%20canon%20g5%20-%20gaping%20gill%20-%20dan%20eats%20red%20hot%20flare.html">
<img align=left src="/caving/photo_archive/trips/2008-11-07%20-%20yorkshire/jarvist%20frost%20-%20canon%20g5%20-%20gaping%20gill%20-%20dan%20eats%20red%20hot%20flare--thumb.jpg">
</a>
The advanced party set off slowly for the main chamber, bumping into some pirates with some balistic entertainment. Eventually joined by the rest of our party, we played a game of glow-in-dark frisbee and then Dan got out our rather more manly pyrotechnics.</p>

<p>Majority exited via Bar, which timed up nicely with the slower derig of flood. Wandered back to van and got changed just as the serious downpours started. Back the NPC, the rain had turned to falling ice and a good excuse to make use of the new fire! Epic meal, mulled wine and infinite rounds of Tesco Value mince pies (surprisingly good, ~10p each) drowned in double cream.</p>

<h2>Sunday: Valley Entrance</h2>
<h3>Peeps: Can't remember... Gergely, Carrie, Rik, William, Alex + others?</h3>
<p>The brave and foolhardy were dropped off in Kingsdale to change in the drizzle, cursing their honour as the others plotted an afternoon's tea-shopping. Abandoned by the van, they dropped down into the main stream, managed to stop the dodgy Hungarian attempting a free dive of a divers only sump (even though we'd been discussing the recent horr story + cave dive rescue only the night before!). A visit to Toyland was made on the return (Mmm, mud, lack of toys).</p>

<p>Jarv crept into the entrance by the light of his Nokia &amp; carefully stalking around the edge of the puddles in order to scare the bejesus out of the returning cavers.</p>

<p>Steady trundle back to London in the rather 'limiting' 62mph minibus. Cruise control was fun behind the lorries though.</p>

