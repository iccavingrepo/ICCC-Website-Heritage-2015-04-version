<h3>Jingling: Thara, Rik, Clewin, Lyndon, Tim &amp;</h3>
<p>
Weather: cloudy, overcast, strong wind from whatever direction, too cold to care
Cave: Jingling Pot
Depth: doesn't look very deep on the a piece of A4 paper (soon to find out)
</p><p>
Unlike IC caving style, at exactly 8:30am, a door swung open; entering
Jim with the most persuasive waking fluid, tea. The effect was clear.
Within ten minutes, a sudden urge to relieve myself of bodily fluid,
forced me out of warm sleeping bag, to battle it out with the morning
coldness.
</p><p>
The cloud hung low in the sky. The trees rattled outside. It seemed
clear to me that I should be inside the hut sheltered by warmth, but
my eagerness to try SRT in the real cave was stronger in my blood,
soon to my surprise we left the hut well before the noon; a real
chance to break the not-caving-before-noon tradition.
</p><p>
After changing into our gear, we began to ascend toward the cave
entrance. My heart was pounding hard against my chest with burning
sensation - fuelling blood to my two legs in attempt to overcome the
gravity and a friction caused by the restricted garments. As I slowly
walked, duck-like and out of breathe, the consequence of wearing
undersized furry became clear. Combining with harness which was
designed without walking in mind, it seemed obvious that I was going
to suffer that day. So I was delighted to finally notice two trees (so
I could stop walking!), which marked the entrance to 'Jingling'.
</p><p>
The two trees hung over the ledge of the cave, which dropped away into
the darkness, providing a perfect shelter to six miserable looking
creatures . With the weather headed for the worst, it was the most
appropriate time to head down into the cave. The rope was pitched to
the not-very-secure-looking tree, and I was the second to enter the
cave.
</p><p>
As I approached the belaying point, my brain started to recall the
distance memory of rope training on the tree. On testing the hard
lock, I needed to put my whole body weight on the rope, suspended in
mid air, high above the cave floor (which I was too scared to look at
all). Although I have clipped my cow tail to a hanger, it was a nerve
racking moment. Eventually, I regained my confidence in the rope, as I
slowly lowered my body, and more weight rested on the lock around my
descender, until it came to the full stop. My fear subsided and I
regained my senses again.
</p><p>
The route took me to a hole on the side of the main shaft. From there
two more pitches and a deviation, led my to the floor, characterised
by two dead, deformed sheep. Chocolate pudding was shared among the
crew, each eager for the  liquid chocolate embedded in the centre.
</p><p>
On the journey back to the surface, I decided to take on 60-metre
single rope climb. My first 10 metres, was mostly spent bouncing on
the rope, after ascending very little. Before I realised that my chest
harness was too loose, I was quite nagged and at least 10 metres off
the ground, not too far to go up or down. It was here that I fully
understand what a number 60 on that paper really meant.
</p><p>
Upon exiting the cave, a cold rain hit me squarely in the face,
draining the warmth I have stored during the last two hours. Looking
back into the cave , something occurred to me. I couldn't see the
floor!
</p><p>
The wind and rain picked up. It was cold. The sky has not darken yet.
It was time for more caves....
</p>
<p style="text-align:right">
Thara
</p>

<h2>Sunday</h2>
<h3>Link Pot: Jutta, Sandeep, Dave, James &amp; Lyndon</h2>
<p><a href="/caving/photo_archive/trips/2006-11-10%20-%20yorkshire%20-%20various/IMGP0593.html"><img align=right src="/caving/photo_archive/trips/2006-11-10%20-%20yorkshire%20-%20various/IMGP0593--thumb.jpg"></a>
<a href="/caving/photo_archive/trips/2006-11-10%20-%20yorkshire%20-%20various/IMGP0594.html"><img align=left src="/caving/photo_archive/trips/2006-11-10%20-%20yorkshire%20-%20various/IMGP0594--thumb.jpg"></a>
We all headed towards the huge 60km long cave system called Easegill in an unusually efficient manner, standing next to the van at 11:30 starting to get changed meant that we might get out during light.
</p><p>
Dave went down to rig the entrance pitch and next went James, this is when I realised that whatever you do on a Sunday it will end up being a faffy day. The pitch was a narrow slot in the rock, only opening out at the bottom. James, although by far the not the widest member of the group got stuck and decided he didn't want to risk going any further and it meant that he had to be hauled out. Not to worry though, he was out and the rest of us went down.
</p><p>
I'd never been into Link before, a slightly more unusual one of the Easegill trips but I don't know why. There was a bit of everything, climbing, crawling stomping and traversing a great trip with lots of pretties along the way. Our underground culinary delights this time were Christmas pudding served with warm vitaminski in a thermos. As with most horizontal trips it seemed to take about 15 minutes to get back to the entrance.
</p><p>
I went up first and met two final year zoology students from Leeds doing a project on bats. Apparently at this time of year they're hibernating and unlike in the movies they're really small and hide in crevices rather than sleep hanging from the ceiling.
<p style="text-align:right">
