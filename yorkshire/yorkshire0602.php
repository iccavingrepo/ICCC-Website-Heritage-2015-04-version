 <p>
 The rest of us ponder what trip to do before finally settling on Diccan-Alum exchange.
 Ropes quickly packed, we join Hugh in Bernies for a brief cuppa before hitting Selside.
 Much debate over who does what cave, but as Andy and I are ready first, we get Diccan.
 Over-rig the first pitch (3 Y-hangs) and completely miss the deviation, so try to get Andy 
 to sort it.  Much confusing shouting &amp; faffing (and getting sprayed on) end up with us
 not bothering with deviation.  Good rigging turned into minamilist rigging as we ran out
 of maillons, but soon at bottom as others appear from Alum.  Brief chat before uneventful 
 exit - Prussiking the main pitch in glorious sunshine with an audience of dolly-tubbers.
 I head back to Diccan to rig deviation in top pitch so prussikers avoid spray.  It's a bit 
 of a swing, but easy enough 'cause of loads of handholds.
 </p>
 <p>
 Back to NPC for tea and medals before leaving for London at 6:30
</p>

<h3> Sunday/Monday - The Journey Home</h3>

<p>
 <i>Jan writes:</i><br>
 Back tyre shreds itself 60 miles out of London.  Doing 70 in the inside lane, Colm's rally
 driving skills save us from taking a roll and parks expertly half way up the bank on the
 hard shoulder.
</p>

<p>
 <i>Andy writes:</i><br>
 I think it's my 22nd birthday, but because I didn't at all climb up to the allotment 
 with a gurt monging tackle sack, then do Diccan, and now it's 2:30AM, I'm too tired 
 to know.
</p>

<p>
 <i>Colm writes:</i><br>
  10:30PM:  Zooming along the inside lane at about 70 when there's a noise from the
  back and the van starts weaving all over the shop.  Manage to steer towards the hard
  shoulder - though I don't think I had much influence on where we were going.  Back
  of the van hits a tree, before we bounce back into the inside lane before planting the 
  van firmly in the embankment.  Hop out quickly to find a shredded back tyre.  Serious
  case of the shakes, so I take a quick jog to the emergency phone to sort myself out.
</p>
<p>
 11:30 No sign of AA, so I phone them on Andy's mobile.  They claim to know about
 our case, but they still ask where I am, the reg no. of the van, details of the accident 
 again!!.
</p>
<p>
 12:15 Truck eventually shows up, replaces the tyre, says the brakes are damaged, 
 so can't drive to London.  I pick up our warning triangle and view the evidence - 
 ripped off branches from the tree everywhere.
</p>
<p>
 It's 4AM, totally light again and the AA promised to be here 2 hours ago.  Some
 garage towed us to some shitty services near oxford where we've watched their 
 video go through three whole circuits.  I moaned to the AA, but the woman just 
 but me on hold for 10 minutes.  Then, I was just getting to sleep in the van when 
 the wankers phone me back saying they'd forgotten about us, and would be
 along at 5.
</p>
<p>
 Now it's 5:30 and we've arrived in SK - Mr AA turned up at 4:30.  We've dumped 
 the shit in stores, and am on way home - scary thing is, people are on their way to 
 work already!
 </p>

