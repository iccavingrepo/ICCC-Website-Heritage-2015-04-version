<p>
<i>Colm writes:</i><br>
 I'd imagine this cave is a very satisfactory rigging trip - though I wouldn't know, 
 as we didn't do it.  Many P bolts, but you only need to use half of them.  
 Bottom pitch is a fine 45m free hang, though with the p-hangers it might 
 be a bit longer.  Sump was absolutely disgusting - I pity the poor guy who
 dived it.  Despite forecast, weather was good, so, on exit I re-rigged the
 pitches to the 'wet-route' to make them easier.
</p>

