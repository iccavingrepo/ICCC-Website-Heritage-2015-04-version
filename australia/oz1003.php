  I arrived in Chillagoe just as it was getting dark, Carol had cooked dinner for me. I was pretty bushed so 
  after quick beer in the local I headed to bed.
</p>

<h3>Sunday</h3>

<p>
  <b>Bridge Cave</b><br>
</p>

<table width="100%" border="0" cellspacing="0" cellpadding="5">
  <tr>
    <td align="middle" width=200 valign=top><h4><i>"...consciousness hadn't quite reached my brain, I gulped 
      down my coffee and we headed off..."</i></h4></td>
    <td>
      <a href="oz1003images/a0021-aps.jpg"><img src="oz1003images/falsefloor21.jpg" align="right"></a>
      <p>
        At 7am, Mark the club secretary and guide for the morning, arrived to take me caving. Consciousness 
        hadn't quite reached my brain, I gulped down my coffee and we headed off on the gravel roads at 
        high speed. In his battered 4X4 along the way he explained there were about 500 surveyed caves 
        in the area, but we couldn't do the main caves as they were inside the national park and we didn't 
        have permits. But he was keen to explore a shaft he had discovered with is son he didn't think 
        anyone had been down. We eventually came off the road onto a vague track into the bush weaving 
        through big termite hills, headed towards two lumps of tower karst. When we reached the right 
        hand lump we continued on foot, around the base there were several holes which Mark looked at 
        with interest, and I looked at with unease. The holes were all guarded by webs, spanned right 
        across and about 5 deep, each holding a fat spider with distinct black and white stripes. We 
        then turned and started clambering through thick bush and over rocks making our way up to the 
        left hand lump, it was pretty spiky stuff, on a couple of occasions I was walking over rocks 
        which would have quite happily diced me up if I slipped. We eventually found the shaft and 
        chimneyed the first bit, the rock was very grippy. A very convenient natural then allowed us 
        to rig the ladder down, with another bit of a climb at the bottom. At the bottom Mark had good 
        and bad news, the cave seemed to continue under a rock arch unfortunately there was a cairn, 
        someone had already been and surveyed the cave probably coming in from a different way.
      </p>
      <p>
        We explored the cave for an hour or so, it was quite extensive and we found another entrance, 
        but it was a vertical chimney choked with some rocks.
      </p>
    </td>
  </tr>
</table>

<p>
  <b>Mushroom Cave</b><br>
</p>
<p>
  After de-rigging Bridge Cave we had a quick look at another walk in cave with some good formations. 
  Which included a large mushroom. By the time we came out it was getting really hot we headed back 
  to the truck and drove back to Chillagoe. Withsuch an early start and despite the long return journey 
  ahead I felt an overwhelming urge to do some faffing, this is of course a natural reaction. I made 
  a cup of coffee and sat chilling in the garden eating biscuits.
</p>

<p>
  <b>Back to Cairns</b><br>
</p>
<p>
  <a href="oz1003images/a0049.jpg"><img src="oz1003images/4milebeach49.jpg" align="left"></a>
  I was just leaving Chillagoe when a big truck pulled up, about 15mins down the road it reappeared 
  in my rear-view mirror and going a lot faster than I was. I pulled off the road and it shot past 
  in a cloud of dust, this is recommended because the truck's generally take up the whole road would 
  rather squash you than risk there cargo. Continuing back I turned North at Mareeba, towards Mossman, 
  dry bush gave way to mountains and rainforest a road called the Rex Highway brought me back over the 
  dividing range and wound steeply down with amazing views of the sea. Randomly I decided to stop in 
  Port Douglas and ended up going for an evening swim off 4-mile Beach. The rest of the journey back 
  was interesting in the dark, along captain Cook Highway, another excessively winding road right 
  along the coast. My final stop before Cairns, was an exposed lookout on a headland, to look at the 
  stars and listen to the waves crashing on the beach below, boat lights blinking out on the barrier 
  reef.
</p>

