<h1>Chillagoe, north Queensland, Australia</h1>
<h2>Weekend Trip 18/19 October 2003</h2>
<p>Jan (and his beast of a 4x4)</p>

<p>
  <i>On the Friday evening a few quick phonecalls to the President of the Chillagoe Caving Club 
  a guy called Alan Cummins, the Secretary Mark Tischler who was staying in Chillagoe and Carol
  the caretaker of the hut, and I had arranged a caving weekend. Left Cairns Saturday midday on 
  the Kennedy Highway west, with 250km to make into the outback. Report and 
  <a href="oz1003photos.php">Pics</a> by Jan.</i>
</p>

<p>
  As we climbed down into the hole I couldn't help noticing lots of spiders, with distinctive black 
  and white markings. Are these spiders poisonous? I ask. Well they aren't agressive, so don't worry, 
  is Mark's reply.
</p>

