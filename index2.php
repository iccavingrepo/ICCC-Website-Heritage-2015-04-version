The second driver, alas, fell ill and so we diverted from the glory of Yorkshire to the homely comforts of the Mendips, and broke into the Belfry. Deep frost during the night, was pretty scary on the roads with the moonlight bouncing off the white bearded fields! Due to the cold we investigated Easterwater Caverns on the Saturday, a cave only a 5 minute stumble from the enormous fire, and found it to be a nice mixture of crawls, clambers and climbs.<br>
Sunday found us with some Charterhouse keys thanks to the kind hut warden, and a splendid photogenic session down GB and a 'sporting' (to the point of drowning) trip down Longwood in furries. 
