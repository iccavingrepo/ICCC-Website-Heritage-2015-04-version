<?php begintextdatebox("Votla Gora 2008","19th July-24th August, 5 weeks");?>
<p>
<a href="/caving/photo_archive/slovenia/2008/survex%20-%202007%20data%20-%20m2%20vrtnarija%20closest%20approach.html">
<img align=right src="/caving/photo_archive/slovenia/2008/survex%20-%202007%20data%20-%20m2%20vrtnarija%20closest%20approach--thumb.jpg">
</a>
<a href="/caving/photo_archive/slovenia/2008/food/lidl.html">
<img align=left src="/caving/photo_archive/slovenia/2008/food/lidl--thumb.jpg">
</a>
How to explain expedition fever? The call of the unknown, the burning desire to stand once again on the pushing front... stumbling home from a caving trip over the moonscape of the plateau to greet the warm glow of the fire in the Bivvi... the ring of the hammer as you place the bolts to abseil down a new pit... the solitude of climbing in darkness, the warm glow of humanity when returned to your company... But such concerns do not convince your supervisor or bank manager to elope for 5 whole weeks.<br>
<br>
So to speak objectively, we have the genuine possibility of connecting Vrtnarija to Sistem Migovec and forming the 2nd longest cave in Slovenia (Postojna, number 1 slot, can only be fully explored by cave diving). The distance between Vrtnarija and M2 (Sistem Migovec) was closed to 30m at closest approach by the end of 2007 (error guessed at circa. 50m). We have a mountain that sits on the watershed between the Adriatic and Black Sea, with possibily the most complicated alpine speleogenesis in the world, and a whole host of secrets to unlock. We have new caves descending into blank mountain, and deep leads near the bottom of Vrtnarija that must be pushed soon before the decay of the bolts puts them beyond reach.<br>
<br>
But anyway, the controls are set for the heart of <a href="/caving/photo_archive/surveys/mig_dem/experimental_jarvist/hollow_mountain_labelled_jarv copy.html">the hollow mountain</a>, Andreja reports from Migovec that there is still some <a href="/caving/photo_archive/slovenia/2006-teacher/Tetley at the bivvy.html">snow</a> in the <a href="/caving/photo_archive/slovenia/2007/martin mcgowan -bivi nights.html">bivi</a>, rope is washed and prepped, Lidl has been abused, and the days are counting down...
