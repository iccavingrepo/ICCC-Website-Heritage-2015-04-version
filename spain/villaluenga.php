<p>
  This cave has a massive entrance, and is just behind a village.  We first 
  tried to pirate this cave on the 2<sup>nd</sup> of April, but decided that 
  it was in someone's garden so we went to 
  <a href="/caving/spain/algar.php"> Algar </a>instead. 
  We returned on the 4<sup>th</sup> of April to complete 
  our mission.  Our plan was to check out the area for a possible night-raid, 
  but when we got there we found that the cave actually had a path down to 
  it through the houses. Unfortunately the village had a large number of mad 
  dago dogs which we had to brave, presumably placed to prevent us from 
  reaching the cave.  Also, the village had a cheese factory whose waste 
  seemed to feed into the stream leading down into the cave.<br>
  The smell was awful.
</p>

