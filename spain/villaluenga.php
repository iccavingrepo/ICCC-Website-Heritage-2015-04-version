<p>
  Luckily the cheese river sank (presumably to produce a cheese sump within 
  the cave?), so we followed a rather pleaseant dried up streamway down into 
  the cave (above right).  Further information about the cave can be found 
  on the 
  <a href="http://www.uca.es/huesped/giex/vr1boca.htm" target="_blank">
    Grupo de Investigaciones Espeleológicas de Jerez</a> webpages.
  They have a survey of the cave (stolen below).  We reached the top of the 
  54 m pitch, but as we were here without permission, we had no SRT kit with us 
  to further the exploration.  This cave is definitely worth returning to, 
  perhaps contacting the local caving groups will sort out any access problems.
</p>

