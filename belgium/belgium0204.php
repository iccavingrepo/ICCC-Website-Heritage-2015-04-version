<h3>Friday - The Horror</h3>
<p>
'Oi you, you f*** Welsh p****'<br>
Said the guy behind me, as I headed with my curry back to the station. Then I saw a guy coming straight towards me 
across the bridge.<br>
'You f***** want some do you'<br>
At this point I was a bit concerned that, A, someone thought I was Welsh, and B, him and his mate wanted to beat me up. 
Fortunately I just happened to be in between the two antagonists.</p>

<p>Back at Stanstead I tried to find a spot to consume my curry and beer 
where the smell wouldn't annoy people trying to 
sleep. But there where lots of people and every seat was occupied, besides in the sterile atmosphere of an airport a 
Lamb Korma could probably be smelt a mile off. About 2am I found a quiet counter to sleep under and awoke early to 
check-in. So ended my unplanned stay in Essex.</p>

<h3>Saturday - The Arrival</h3>
<p>James and Andy arrived around 9am at Charleroi to pick me up. We zoomed 
back to Hans-sur-Lesse and grabbed a delicious, 
and huge, ham and cheese omelette with chips, for breakfast. By the end of breakfast we had a plan, 3 hours later we had 
packed four tackle bags. We finally headed out of town at 2pm, stopping briefly to buy a map (and Hoegaarden for apres 
cave). We located Mont-sur-Meuse and the hire car convoy continued through the rolling hills of the Ardennes, before 
coming to a stop outside a very strange looking church, with Trou d'Eglise 
across the road in an enormous shakehole. 
After a group photo on the village green in the late afternoon sunshine, 
the intrepid cavers set off.</p>

<h3>The Cave</h3>

<p><b>Trou Bernard, Central Route - Jan, James, Darryl</b><Br>
There seemed to be a whole lot off faffing going by Andy, Rik and Goaty. 
When I ab'd down I saw the cause; their route 
was an awkward side passage leading off 3m down the entrance pitch. We slithered on down a series of narrow, well 
smoothed pitches - les Chicanes, Puits de l'Oppo, Cureton - and we reached 
the surprisingly big final pitch. We then 
waited for two French guys, who for some inexplicable reason were using dynamic. So we had some time to kill, discussing 
whether it was actually impossible to teach some people to learn certain concepts, not sure where that one came from.
I admit, I was faffing on the rigging a bit, but it required a big stretch over nothingness. Then James and Darryl 
helpfully started debating how unstable the boulders for my backup looked - cheers guys! A nice free-hang to a large 
ledge and then a sloping pitch brought us to the bottom, and a maze of twatty rifts (la Labyrinthe). After considerable 
effort not finding the connection through the Meandre SSN, we headed out. Of course James, going up first, then spotted 
the Meandre leading off the from the large ledge, but we didn't have time 
to explore.
</p><p>
The bitch of this cave revealed itself on the way out with the evil combination of smooth tight pitches that were 
slightly wedge shaped and obstinate tackle bags. But with healthy amounts of swearing and grunting we extracated 
ourselves and, in what we thought was excellent timing, ascended the final pitch at the same time as the others were 
gratefully squirming out of their hole.</p>

<p><b>The Apres Cave</b><br>
Our evening's meal consisted of some slap-up complimentary peanuts back at Bar Bus Stop. Washed down with copious 
amounts of Belgium beer - in the interests of drunkeness all varieties were rigorously sampled. Driven by hunger I was 
briefly tempted to buy an ice cream. Around 2am the excitement of the last 36 hours and lack of food caught up with me 
and I stumbled back to the hostel, though I negotiated the stairs with ease.</p>

<h3>Sunday - The Faff</h3>
<p>
Sunday seemed to consist mainly of searching for petrol, then searching for the Maison de Speleo and then searching for 
a cave. We eventually succeeded in all three, but unfortunately just a bit too late to do the cave. We were really torn 
up about that one, so we had to eat our sorrows away in a nice café across from Guillemins train station in Liege. We 
said farewell to Goaty, as he returned by train to the flat lands and we started our own journey back to Blighty.
</p>


