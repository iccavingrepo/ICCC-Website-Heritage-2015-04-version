balls where previous avalanches had thundered down. In some places the
snow left was cracked with deep ravines.</p>

<p>We stayed at my house, with my mum to cook delicious meals for all of us.</p>

<h3>Sunday: Snow and Jacuzzi</h3>
<p>The next day we decided to go to Bohinj with a train. Plan A was to go
cross county skiing, but because there was not enough equipment (or
not correct size) we choose plan B  - sledging.</p>

<p>It turns out as a great fun where you can actually sustain more
injuries then in caving.</p>

<p>Afterwards we went to a local 'Wellness' centre (i.e. Waterpark / leisure centre) and enjoy ourselves in
a pool with climbing wall, 2 Jacuzzis, artificial sea cave and two
slides. We did relax a lot, but even here got a few more injuries. (Jarv: I managed to sustain a fairly extreme injury in my quest for the fastest route down the waterslide, went slightly too fast on a corner and got thrown up onto the ceiling of the tube where the dry plastic burned a hole straight through my trunks and into my hip)</p>

<p>The whole trip was amazing with stunning weather and we learnt that
sledging and sliding is much more dangerous then caving. So next time
