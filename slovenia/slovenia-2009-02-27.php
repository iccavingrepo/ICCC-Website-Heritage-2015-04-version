<h2>Saturday</h2>
<h3>Saturday: Poloska Jama</h3>

<p>We left off at the crack of dawn (for real!) in order to avoid having to walk through mushy snow on the top of Krn. The "cave" team was composed of me, Jarvist, Tim, Andy and Andrej. Jana and Andrej's son Matej and daughter Andrea acted as "porters" to help us carry the mountaineering gear we did not need back down. Jana's mum also accompanied us on the way up to the entrance and provided invaluable logistic support.</p>

<p>The walk up was certainly... memorable. The path climbs steeply up in a wood. The plan was to gain altitude as quickly as possible to avoid rock fall and avalanche. The first snow we encountered was an avalanche. After that, some more walking leads to the top of a col. We stopped here next to someone's  summer huts, at this point we are standing on several meters of snow and decide to don our crampons. We are only mildly unprepared with me wearing crampons that do not fit on my shoes and jana completely devoid of any snow walking equipment. The next section of the walk required us to walk on a very steep snow bank with several icy sections. The Fratniks helped us clumsy english "mountaineers" by rigging some safety ropes and providing assisrance. After a lot of humming and harrring we made it to the entrance of the cave, had our sandwhiches and got changed. Matej, Andrea and Jana collected all our stuff and we started underground at about 12pm.</p>

<p>The entrance of the cave is a rather squeezy squeeze under a boulder. Some contorted caving leads to a (hand) rope which allows us to overcome a large boulder. Andrej points out the name of the Ljubliana club who first found this upper entrance etched onto the wall. We surpass the large boulder and hide the rope ("keeps away idiots" informs me Andrej). Soon enough we reach the roped series, Andrej claims that it is 120 m. I first think, a 120 m pitch. But no actually the whole extent of roped section in the cave is perhaps that deep. The pitches are fine pitches, with well placed anchors. For some odd reason the backups have all been cut out... I'm sure whoever did that knew what they were doing(!).</p>

<p>At the bottom of this srt series is a pretty pool. This is how far Jarv and Andrej had been a few years previously when rescuing some lost climbers. The route countinues along a meander. For the normal route one would stay high and go down some free climbs. Andrej decides that he wants to explore another route, through the bottom of the meander. The route is rather reminscent of the more pleasant sections of captain K, in other words it is not spacious and rather strenuous. At (what seems like) great length we reach a vaste chamber. Andrej and Tim shoot down free climbing. I am following them feeling less and less happy with the chossy rock and the large drops, but Tim ensures "it's cool". When Jarv and Andy reach us they inform us that they have found the metal eye for the double rope, so I carry I rope up for them. Faff faff faff, set up a double rope, faff faff faff abseil down. Faff Faff aff, the faffinf faff rope faffing does faffing come down. Climb faff up. Faff under an overhang, free rope. Faff rope down.</p>

<p>After this chamber we are into what is actually a very pleasent bit of caving: Andy describes it as a steeper version of OFD (no decoration tho!). Frankly I dont have much memory of the stomp out, near the entrance was another tight-ish squeeze and a very pleasent bit of high level traversing in rift. We got out shortly before 5 pm. Not too bad for some old fogies (and me and TIm)!.  </p>

