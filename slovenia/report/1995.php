<dt><a href="detail.php?cavename=M22_Venus">Venus Cave</a></dt>
<dd>When it was found the entrance of this was a hole about 8cm diameter which was sucking
air in so strongly that flies were getting trapped (hence the name). Half an hours excavation 
with a spade revealed a 10 metre pitch leading off into a series of low horizontal passages  and
a small chamber with calcite formations, these are the first formations discovered on the Migovec
plateau.
