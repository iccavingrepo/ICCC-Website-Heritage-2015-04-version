<p>
At the bottom of this shaft the ways on are very tight
but half way up another rift leads off (Shreddies Revenge) which goes on for about 50m until the
way on becomes very tight. Another 3 hours of hammering  and we were through Orsazmatron
and Nutcracker squeeze  to the head of a 15m circular shaft. At the bottom of this a small hole in
the floor led to another 30m shaft which again didn't go anywhere at the bottom but at the top a
swing gave access to what looked like a large chamber. The Large chamber turned out to be the
side of a large phreatic tube 6m wide and 4m high which turned out to be 250m long with many
large shafts and aven's leading off it. From this point exploration was continued with the aid of a
three man camp in this tube (Club Mig). A number of the leads were looked at but many remain
untouched, a trip in the last week found a very large free hanging shaft (Godzilla)  it took a rock
4.5 seconds to hit the bottom, unfortunately all the ropes we had in the cave at that time did not
reach the bottom and further exploration would have to wait until our next trip.
</dd>

