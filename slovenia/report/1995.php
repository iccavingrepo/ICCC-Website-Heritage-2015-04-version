<h2>Summer Expedition 1995</h2>

<p>
The ICCC and JSPDT 1995 Expedition was much more productive than the previous year, 
the most notable discovery was Torn T-shirt Cave(M18) which was pushed to -232m with 
going leads.
</p>

<h3>Introduction</h3>
<p>
Following limited success on our expedition in 1994  where 23 people spent six weeks  on Migovec 
not as many people were prepared to spend another six weeks in 1995. A much smaller group of die 
hard optimists plus a number of keen unsuspecting  freshers made the trip in 1995. The total number 
of people on the expedition was thirteen, but in fact only eight people spent six weeks on the plateau 
and the rest spent shorter periods anything from one to four weeks.  From our expedition in 1994 we 
encountered a number of problems which we hoped to address this year, they were:
</p>
<ol>
 <li><p>Having a low level base camp near the road was a waste of time, it meant far too much
 time was spent sherpering gear back and forward, it encouraged people to sun bathe instead 
 of exploring caves and it wasn't cheap either.</p>
 <p>
If we were going to get away with just having a high altitude bivi we would have to work out a 
way of getting all our gear up the 1000m climb efficiently and we would have to completely rethink 
our food  (which consisted of a lot of fresh food and tins) and water collection which was slow and 
inefficient. Additionally this year we were more selective about who we brought on the trip- freshers
had to be reasonably self  reliant and people who dossed in 1994 were not encouraged to come.</p></li>

<li><p>Finding caves was not just a simple case of finding the right hole and abseiling down to great 
depths (as we foolishly first thought!), caves on Migovec are more stubborn than this and  usually
negotiating tight rift or digging and hammering are required in order to proceed . 
</p>
<p>
This year we brought a full digging kit (hammers, chisels, crow bars and spades) and  we made
up a rule that if a cave was  to be deemed not to go then two independent groups must agree this is the case.
</p>
</li>

<li>
<p>
There are thousands of holes on the Migovec plateau and knowing where to look is not easy, it is 
also difficult to know if anyone has been there before -- The only practical solution is to log and 
number all entrances and paint the number on the entrance. In order cut down the number of holes 
to look at, we decided to return to the plateau when it was in deep snow in order to find and log the 
draughting holes.</p>   
</li>
</ol>

<h3>Logistical Considerations</h3>
<p>
Our first task was to get all our equipment and food up the hill as quickly as possible with the 
minimum effort. The first possibility we looked into was a helicopter but this had two main problems
associated with it; firstly at five hundred pounds per trip it was a little on the expensive side and 
secondly as we were officially not supposed to camp in the Triglav National Park we thought this
would draw unnecessary attention to us and could lead to problems later. The method we eventually
settled on was to get the local farmer to carry up our gear with a small tractor, he could only go about
half way (to the shepherd huts) but this proved sufficient,  and was also a lot cheaper (about eighty
pounds each way).
</p>
<p>
If we were going to spend all our time up at the bivi then we could not afford to take fresh or 
tinned food as it would be too bulky and  in the case of fresh food would not last long enough. 
One of our members joined the BEC on one of their five day camps in Darren Cilau (The Restaurant) 
and the system of dried food they have developed over a number of years was copied almost identically. 
As bread will not last six weeks flour was taken up the mountain and used to make chapattis. The only 
non dried foods we took were cheese, lard, chocolate and alcohol.
</p>
<p>
Collecting water on the 1994 expedition had been a long and time consuming affair (half a days work 
for three people every day)	which consisted of someone abseiling down a shaft and filling bags of snow 
which were pulled to the surface and melted in the sun. This year we used a different technique which 
proved more effective- a barrel was left in the shaft to fill with water which was then pumped from the 
cave with the aid of a small semi rotary hand pump and 40m of hose pipe.  This was only necessary about 
half of the time as  the rest of the time it was raining heavily and water could be collected directly on the 
surface with the aid of  tarpaulins and barrels.
</p>

<h3>Caves Discovered</h3>

<dl>
<dt><a href="detail.php?cavename=M18_Torn_Tshirt">Torn T Shirt Cave (M18)</a></dt>
<dd><p>This cave was pushed from last years end . The cave has a tight entrance which leads to 
an 8m climb and then a 25m pitch (Godzuki), at the head of this pitch there is a way on which 
leads to 40m of rift and becomes too tight. At the foot of the pitch a climb over boulders and a
false floor leads to a further 6m climb and a 6m pitch to a chamber, at this point the cave 
becomes a tight rift (The Shreddies Series) which goes on for a further 100m until it becomes 
very tight, this year with the aid of a hammer and chisel we got through a first squeeze 
(Optimisqueeze) which leads immediately to a second squeeze which pops out at the top of a
thirty metre shaft (Turtles head squeeze). 
</p>

