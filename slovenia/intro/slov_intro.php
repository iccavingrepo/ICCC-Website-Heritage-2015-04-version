<h2>Introduction</h2>
<p>
<img align=right width="300" src="mig.jpg">
Imperial College Caving Club has run joint expeditions with <a href="http://www.pdtolmin.si">JSPDT</a> to the Julian alps in Western Slovenia most summers since 1994. The destination is Tolminski Migovec, a large undulating limestone plateau located about 6km North of the town of Tolmin within the Triglav national park.
Located midway between <i>Tolminski Kuk</i> and <i>Vogel</i> at approximately N 46 15' E 013 45' (<a href="http://maps.google.com/maps?f=q&hl=en&q=46.25+13.75&ll=46.249971,13.74999&spn=0.028252,0.086517&t=k&om=1">Google Maps Satellite Data</a>), the large area offers enormous scope for further cave development. 
</p>

<p>Expedition base camp is located at 1880m above sea level on the western edge of the plateau, with the <a href="/caving/photo_archive/slovenia/1996/bivi%20in%20the%20summer%201996%20by%20mark%20evans.html">Bivvi</a> situated in a large shakehole benefiting from the shelter of a [distinctly unstable looking] rock bridge.
</p>
