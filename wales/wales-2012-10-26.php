<h2> Saturday - Agen Allwedd </h2>

<h3> Everyone </h3>

<p>This past weekend caving trip was definitely the most difficult and most rewarding experience that I have had so far while studying abroad in the UK.</p>

<p>As a fairly tall person who's not exactly lanky, I could not wiggle through certain holes and crevices as easily as the other people that came on the trip and needed a lot of help. I was beginning to feel frustrated that I was holding the group back but then Jana made a very good point. She told me it wasn't about rushing through the cave, but experiencing the cave, so you don't need to worry about being too slow.</p>

<p>So although I struggled a lot caving and was excited to see the Welsh sunlight, I'm really happy I went caving. I felt the kindness of people I barely knew and how someone can literally get you out of any situation.</p>

<p>The rest of the weekend was also great with fun caving games and getting to know students from Imperial, who were all really nice. Also the food was fantastic! </p>

<p>I would recommend that if you like the outdoors caving gives you a great perspective of the great things you can see from below the surface.</p>

<p style="text-align:right">
Rebecca Dykes
</p>


<h2> Sunday </h2>

<h3> Ogof Cnwc - Jan, Oli, Konrad </h3>
<p>Fresh from an extra hour in bed, and despite the excesses of Saturday night, Konrad, Oliver and I went to investigate Ogof Cnwc (pronounced canook, or k-nook, or cannuck), or Old Prices dig.</p>
<p>The cave is in the last quarry on the left, you can't miss it, its about 5-10mins past Pen Eyrryryryry-er, there was also the remains of a sheep carcass near the entrance when we got there, but that can't be relied on in future.</p>
<p>On Saturday night I was speaking to some Cardiff cavers who stopped in for a tea, at Whitewalls, and they said you could do it as part of a through trip with Daren Cilau, apparently only connected up 3 or 4 years ago. Intrigued by this, I enquired with the hut man (John) if he knew of it, and hesistantly he said ye-es (was this alternative entrance to Daren some guarded secret I thought...) how does one get into the cave, I enquired further, I suppose I could lend you a key, if you wanted, he said, well if you don't mind that would be nice, I replied.</p>
<p>Several cups of tea, a chapter of the The Pot Hole Mystery and a quick change later, we were squirming our way through the entrance dig, the entrance took about 15mins, its a bit muddy and there are flat out bits, but no more than 3 or 4 body lengths between mini chambers, and there are no awkward corners, so it's an ok second trip for a keen fresher.</p>
<p>Arriving in a big chamber we turned left, and met up with Dewi and his friends. They offered to guide us for a bit, we backtracked past were we had come and down Busman's Holiday, a big horizontal passage, at one point i noticed the whole roof of the passage seemed be sagging down, you could look in between the layers of bedding where they seemed to be seperating, we then had to walk underneath. Dewi's group turned back and we carried on alone, they told us to follow our nose, then when we got to a dead end, we should look behind us on our right behind a boulder, and we would see a big chain ladder going down into Antler Passage. </p>
<p>It took us about 30mins to get the ladder, then I was expecting to climb down into a big passage, but instead it was a chamber with no obvious way on, apart from a dig by some trickling water. We turned back to get out early for the trip home, but it turns out this dig was a short crawl that lead to the Antler Passage.</p>
<p>It would be a good weekend to do a return visit to complete the through trip. And also other caves like the Draenen round trip and Craig Y Ffynonn.</p>

<p style="text-align:right">
Jan Evetts
</p>


