<h2>Friday</h2>

<p>Assembling around 6PM in stores, a mixed crew of innocent young freshers and experienced cavers attempted to collect their gear in reasonable time. Despite valiant efforts, we didn't get off till 7:30. The journey was quite reasonable however, and after a speedy night shop we had arrived at our hut by 12. After a quick supper of spaghetti in true student fashion we slid into our sleeping bags to enjoy a pleasant nights serenade of snoring. </p>

<p style="text-align:right">Oliver Myerscough</p>

<h2>Saturday</h2>

<p>We mostly awoke quite early, eager to set out caving. Stragglers were enticed out of bed by cups of hot tea and the smell of a hearty breakfast. Our bellies full, we piled into the minibus and began the drive to OFD. </p>

<h3>OFD - Top Entrance</h3>

<h3>Top Waterfall & Poached-egg climb: Jarv, Clare, Jonny, Saber, John, Sally</h3>

<h3>Ed's Shortcut/Salubrious: Al, Nia, William, Fiona, Kee, Sam</h3>

<h3>Round about everywhere climb: Martin, Nico, Oliver, Zhen, Konrad</h3>

<p>Splitting into three groups, consisting of several pieces of fresh meat led by a couple of more grizzled cavers, we clambered down into OFD. Consisting of Martin and Nico in the lead with Zen, Konrad and I in tow, my group entered the cave around 11:30 </p>

