My memory of our actual route is poor to non-existant, so I shall report it mainly with names of the formations we passed, possibly even in the correct order. We began by heading towards Judge and Trident, but peeled off early. On our travels we passed the Frozen River and saw numerous stalactite formations. This route brought us through some rather tight squeezes which I was very glad not to be carrying a tackle sack through. In the process of moving between these there were plenty of chances for free climbing and traversals, which I enjoyed particularly. </p>

<p>Following the river down to Trident and Judge brought us to the turning point in the trip, as we began to head out. On our way we had another chance to get our feet wet after I choose the muddy path to the Mini Columns, a descision that may be considered unwise in retrospect. Following a quick break there we returned to the entrance by a rather dodgy (read: intensely enjoyable) free climb down several false ledges. All in all, a round trip of 5-6 hours and an excellent crash course in caving.</p>

<p>This being my first real experience of caving, I found the size and variety of OFD quite surprising. I'm not quite sure what I was expecting, but the cave did not disappoint.</p>

