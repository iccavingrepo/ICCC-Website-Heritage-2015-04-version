Upon returning to the hut, we quickly got stuck into producing a good solid meal. This was a very successful experiment of Jarv's: home made pizzas. It turned into a real group effort, with bases delivered to an expert assembly line outside before returning to the kitchen for cooking. There was so much dough that we had fresh food supplied for much of the evening.</p>

<p>After taking our fill of dinner there was a chance to see the infamous caving games that I had heard so much about. The squeeze machine proved popular with us new people, but the many more experienced of the group chose, perhaps wisely, to spectate. </p>

<p style="text-align:right">Oliver Myerscough</p>


<h2>Sunday</h2>

<h3>Dossers: Nico, Clare, Saber, John</h3>

<h3>Town Drain, White Lady, Bridge Cave: Al, Fiona, Zhen, Konrad, Jonny, Kee</h3>

<h3>Little Neath: Jarv, William, Sally, Oliver, Sam</h3>

<p>Somewhat tired from the day before, we rose at more civilised hours on Sunday. After an even more substantial breakfast, we again split into three groups. Nico, Clare, Sabre and John choose to remain at the hut, while Alex led a group on a relaxed tour of some smaller caves about the Little Neath river. Jarv and William led Sally, Sam and I down Little Neath cave.</p>

<p>Crawling down the river entrance was pretty tight, but with my wetsuit on I was nowhere near as cold as I had expected. In front, William led at a blazing pace, setting an enjoyable test for us freshers. We took a break at the bottom of the cave before retracing our footsteps briefly and heading into the Canal. The roof quickly fell and the water deepened, but this section proved easier than walking as we could drag ourselves along, half floating in the stream. Seeing foam on the roof from last time the cave flooded was slightly unnerving.</p>

<p>Reaching the rescue dump at the Canal's end, we started back towards the exit. In no time we found ourselves back at the entrance tunnel. Just after entering the tight section we suffered two lighting related grievences. First William discovered his irreplacable battery box lid was missing, which we could not find, and shortly after my own batteries died. Fortunately changing them did not prove too difficult despite the restrictive walls.</p>

<p>Upon leaving the cave with no fatalities we returned to the bus and paid due reverence to the stash of food left there. Back at the hut we were presented with yet more food, in the form of spaghetti and mushroom sauce, prepared by those who stayed behind. After dining we got away quite quickly, leaving the hut around 6:30 and reaching stores at 11.</p>

<p style="text-align:right">Oliver Myerscough</p>


