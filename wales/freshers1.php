<h2>Wales 12<sup>th</sup> and 13<sup>th</sup> October 2002</h2>

<p>
<a href="images/croydenhut.jpg"><img src="images/croydenhut_tn.jpg" align="left"></a>
In a rather uncharacteristic moment of efficiency, we actually managed to pack all the kit and get everyone loaded in the minibus ready to leave before 8. All the equipment strapped to the roof (It ain’t gonna rain) and all the new members rounded up after getting lost looking for food in the union and we still managed to leave before 8? That’ll be the day! At least that meant we arrived at the Croydon Caving Club (Ystradfellte, Brecon Beacons, left) at an acceptable hour. We needed to be up early the next day as the custodians of Ogof Ffddun Dyu (OFD, the deepest cave in Britain) in a ICU-like display of bureaucracy refuse to issue access keys after 11am.
</p>

<p>
By the morning the Welsh rain had gone and it was turning sunny. We decided over the morning fry-up to run 2 touristy-style trips into the top entrance of OFD, and another to Pant Mawr for those who had completed ladder training. Due to the lack of rain the walk to Pant Mawr was pleasant but the trip was soured by Jan unfortunately dropping his hideously expensive camera 20m down the entrance shaft. Well you live and you learn.
</p>
