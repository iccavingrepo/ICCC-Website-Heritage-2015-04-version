<p>
The two trips into OFD were less technical, but with over 50 km of passageway to explore, route finding even in the areas close to the entrance can be a process of trial and error. A couple of hours were spent looking at the formations known as the 'Wedding Cake', 'Judge' and 'Trident', and also exploring the poorly named 'Big Chamber Near the Entrance'. Andy bumped into an old acquaintance on the way out who invited ICCC over to the '18th Hole' WSG dig, although the drills and explosives sounded fun, we had some serious caving to do on Sunday.
</p>

<p>
<a href="images/ofdentrance.jpg"><img src="images/ofdentrance_tn.jpg" align="left"></a>
It was another miracle which saw us at the infamous South Wales Caving Club (SWCC) HQ to sign ourselves in for another trip into OFD. The hut has changing rooms and lovely warm showers, which was welcome as the weather had taken a turn for the worse. The trips were all going to be in OFD, 5 minutes from the hut. No-one wanted to go on the 40 minute walk in the rain to Pant Mawr. Visibility decreased, the rain got heavier, and the wind picked up. We were glad to get underground where it was actually warmer and drier! Even those of us who ended up in the main streamway wading in waist deep water were glad to be out of the freezing wind. The efforts of those who managed to climb down a taxing rift, and down a waterfall using an iron ladder had their efforts rewarded by experiencing one of the finest streamways in Britain. A trip upstream led to the Top Waterfall, a visually impressive wall of water, and the limit of exploration in that direction.
</p>

<p>
Clewin's team ventured into Cwm Dwr, the middle entrance of OFD, just a couple of hundred metres from the SWCC hut, perfect! After experiencing the 'Grade 2 Confined Space' (where was that sign stolen from?) the time-honoured ICCC tradition of getting lost in the Cwm Dwr Boulder Choke was accomplished. Meanwhile the remaining party entered OFD top with the specific aim of completing the Selenite-Edward's Shortcut roundtrip. Did they complete it? Err, no.
</p>

<p>
<a href="images/janstrawberries.jpg"><img src="images/janstrawberries_tn.jpg" align="right"></a>
The drive home was really unpleasant. A pant-filling moment in the high winds on the way over the Severn Bridge was the low-light, while Shed discovering the eat-as-many-strawberries as you-can at one of the service station on the way home was the highlight (for all concerned).
</p>

