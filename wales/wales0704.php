<h3>Saturday - Ogof yr Ardd</h3>
<p><b>(Cave of Arse) - Swansea Valley</b>
</p>
<p>
Andy Sewell mentioned a little known cave which was supposed to be good, so we headed off to check it out, 
despite a conflicting account that some Oxford cavers had given it one look and decided it was too shonky. But we were unperturbed, well possibly a little, as Hilary had been one of the cavers.
</p>
