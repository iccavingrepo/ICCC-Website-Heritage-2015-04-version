<h2>Wales 26<sup>th</sup> and 27<sup>th</sup> October 2002</h2>

<p>
  Photos and report courtesy of Darryl Anderson</p>

<h3>Saturday</h3>
<p>
  <b>OFD 2</b><br>
  <i>Darryl, Colm, Jani, Moritz, Philipp</i><br>
  This was a first caving trip for Jani, Moritz and Philipp. Colm knew the way so 
  he led us down to Gnome passage and down Selubrious passage, stopping off to see 
  the trident and the judge on the way. From there we went down Maypole inlet to 
  the OFD 2 streamway, with me and Colm taking photos along the way. We went upstream 
  for a bit but didn't get as far as the top waterfall before turning back. We then 
  climbed back up maypole inlet and returned the way we came, stopping off at the 
  mini columns on the way, including a ridiculously pointless squeeze where I 
  managed to get a photo of Jani. Time underground 5 hours. The evening involved 
  drinking beer, cards, drinking gin (as we'd run out of beer), table traversing, 
  and yet more drinking.
</p>

<h3>Sunday</h3>
<p>
  <b>OFD 2</b><br>
  <i>Darryl, Rick, Jerome</i><br>
  Not wanting to go down Cwm Dur today,  I took the 2 guys who did that on saturday 
  into OFD 2. After a few route finding problems with White Arch series, and a quick 
  detour to see the mini columns, we eventually found Gnome Passage and from there we 
  went down Edwards Shortcut, with the slippery traverse, and on to Selenite passage. 
  After reaching the top of Maypole inlet we decided not to go down so we headed out 
  via Salubrious, including the detour to trident and judge. Time Underground 4 hours.
</p>

