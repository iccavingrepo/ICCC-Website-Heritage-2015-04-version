<h3>Sunday</h3>
<p>
  <b>Pwll Pindor</b><br>
  <i>Darryl, Joachim, Bela</i><br>
  Tired and hungover I emerged from my bunk at 10.15 to find Martin making breakfast. 
  Somehow during breakfast I had volunteered to do OFD, so we headed over to SWCC to 
  get a key but as it was noon when we finally got there I was saved by the fact that 
  the hut was locked up for the day. Martin suggested going to Pant Mawr to look at 
  the entrance to a WSG breakthrough called Pwll Pindor. The van had a bit of trouble 
  getting up the track after passing little neath but after a brief push it got 
  going and we carried on to Pant Mawr. After returning to the van after walking to 
  the entrance (and pinching some logs) only me, Joachim and Bela wanted to cave (and 
  I was teetering on the edge of not going) so we headed down the entrance, leaving 
  Colm and Martin in the van.
</p>

<p>
  The entrance is a 12m climb down scaffolding (past some lost frogs). A left turn 
  after the entrance leads into a flat out bedding-plane crawl. After a while you come 
  into a well decorated chamber which had more pretties in it than our trip in Draenen 
  had on saturday. Another good thing about this chamber was that it was high enough to 
  sit down. Two ways on were explored, one of which consisted of another well decorated 
  bedding plane that closes down to become too tight. The other way was into "Ted Moult 
  Passage", a bedding plane crawl on a lower level leading to a low, pretty passage. 
  After this and ignoring the other way from the entrance we headed out aftre 1.5 hours 
  of hard caving.<br>
  Looking at the <a href="http://www.wsg.org.uk/docs/pin_sur.htm" target="_blank">
  survey</a>, we could have gone further as we had missed the gours and Bishopsgate but 
  we had dismissed the turning. Oh well, one for another hungover day in South Wales.
</p>

