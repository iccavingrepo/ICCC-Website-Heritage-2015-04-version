<h3>Saturday - Ogof Draenen</h3>
<p>
  <b>Linear trip</b><br>
  <a href="images0303/entrance.jpg"><img src="images0303/entrancelr.jpg" align=right></a>
  <i>Darryl, Martin, Andy, Boris, Helen, Bela</i><br>
  We were all woken up at 8.50am by pete, who had finished making breakfast (bastard, he 
  said 9.30 the previous night), but it mean't we could get all the faffing out of the way 
  and still get underground before 12.  Weather was atrocious, so we decided to do Draenen, 
  a cave which I had never done before, so I decided to do the easy linear trip so with 
  Martin leading we headed in (after wrestling with the combination lock for a few minutes).
</p>

<p>
  <a href="images0303/joachim.jpg"><img src="images0303/joachimlr.jpg" align=left></a>
  The entrance of this cave is squalid and wet but various climbs and squeezes made it quite 
  fun. After getting through the entrance series boulder hopping is the main activity until 
  Indiana Highway, where you start traversing. Boris turned back at the roped section 
  (which, as it turns out, is pretty easy), so we went on to see the small pretties at 
  megadrive. I took some photos before we headed back to pick Boris back up. When the group 
  was reunited again we made our way to the streamway. More small pretties were seen here 
  before we headed out of the cave via wonderbra (or as Martin put it, "hanging death"), 
  to emerge into sunlight after 5 hours underground. The pub was closed when we got out so 
  we had to wait in the van for the other guys.
</p>

