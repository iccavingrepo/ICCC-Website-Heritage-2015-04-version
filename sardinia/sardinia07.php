We got off to a rather inauspicious start once we had picked up the 
hire cars at Alghero airport. &nbsp;I couldn't even get mine to start (for 
future reference in a Renault Scenic you have to press the start button, 
depress the foot brake and sing Auld Lang Syne backwards while farting 
the Marseillaise) and after stalling at the car park exit all three 
cars separated in three totally different directions. &nbsp;Once we had 
regrouped half an hour later in the same car park my nerves were a 
little frayed and I could read in everyone else's eyes the same 
thought: fuck, if we can't even get out of the airport, how the hell 
are we going to cross Sardinia to our villa? &nbsp;Remarkably everything 
ran smoothly after this hiccup and we drove in a perfect convoy to 
Irgoli with Gergely as mother duck.<br>
<br>
There was still plenty of light left in the day when we arrived, so 
Jarv and I took our cars for a spin to the beach and then up into the 
mountains. &nbsp;Here we got our first taste of the grey roads on the map 
which live in that netherworld between tarmac and footpath. &nbsp;`Hairy' 
doesn't quite do justice to the vertiginous drops and acute hairpins. 
The mixture of terror and reckless abandon on Jarv's face as he dodged 
rocks and sheep in the Mercedes was priceless. &nbsp;We stopped at the top 
and had a bimble around the ridge. &nbsp;The views into the valley beneath 
the setting sun were gorgeous and only reluctantly did we return to 
the villa that evening for the first of many pasta and tomato sauce 
suppers.</p>

<p align=right>Tom Brown</p>


