  Today we took a 95m rope and SRT kits for a very exciting walk in the
  mountain. Following Jutta's directions, in turn derived from the toothless
  locals, we searched for this entrance far too high up in the mountains. Having
  mistaken a 30cm large red lichen for a 30cm large red paint spot, we spend a
  good hour or two exploring little caves on the slope of Tiscali.
</p>
<br>
<p align=left>
  Back at the car as dusk fell, we cross-referenced a million incomplete and
  contradictory reports to decide that the lower path visible from the top was
  actually a better bet. We zoomed off (without kit) into the approaching dark.
  By following the path straight along (1.2km from car-abandoning spot) we
  reached some zigzags and suddenly found ourselves at a clear cave - Grotta
  Monumental.<br>
</p>
<br>
<p align=left>
<a href="/caving/photo_archive/tours/2007%20-%20sardinia/gergely%20ambrus%20-%20IMG_0186.html">
<img align=right src="/caving/photo_archive/tours/2007%20-%20sardinia/gergely%20ambrus%20-%20IMG_0186--thumb.jpg">
</a>
This was being occupied by the Sardinian Unabomber. He had a shack constructed
  from drystone limestone (topped with a couple of solar panels), a dog, a
  shotgun and a herd of goats. The goats were crazy - left to camp out in
  amongst the stalagmites. When I was first there, I called out a few hellos. No
  response. It was only after the second party braved the now awakened guard-dog
  to explore the depths of the cavern with lights that they noticed the Goat
  Man. In amongst the enclosure, feeding his herd, he paid us absolutely no
  attention whatsoever. Rather eerie it most certainly was. Pity we had no
  camera.
</p>
<br>
<p align=left>
  Down back to the path - location of Tiscali should now be obvious from the
  SMCC map. Clambering off the cliff-face. Discover a wire-fence - ah hah, my UK
  caving sense is tingling. More desperate clamber the far side of the fence and
  JESUS! That's a big drop! Which happens to correspond with a missing section
  of the fence. Just time to GPS, cairn, add an extra arrow further from the
  path and then head home. Large red paint-spot entirely absent.<br>
</p>
