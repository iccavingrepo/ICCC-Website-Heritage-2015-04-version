After our sweat-inducing adventure in Su Palu on Sunday - James, Tom and I resolved to form a crack team capable of penetrating the cave's further reaches spurred on by the promise of "Sardinia' most decorated passage." Having lost the two Bens to Sandeep's temptingly easy Su Bent trip, Team Glamour recruited Jutta and forged an uneasy alliance with 'Team Merc', comprising Jana, Tim and Jarvist 'Schumacher' Frost.</p>
<p>
No more than half an hour into the cave, and unlucky Jarv felt a rumble from deep inside the wetsuit. Caution being the better part of valor, he opted not to risk filling skin-tight neoprene with the rejected contents of his poorly guts and made a swift exit. The rest of the team continue and we made quick progress to the point halfway along the Alta Loma where we had turned back on Sunday.</p>
<p>
<a href="/caving/photo_archive/tours/2007%20-%20sardinia/jarvist%20frost%20-%20su%20palu%20-%20first%20chamber%20team%20shot.html">
