<h3>Team Glamour:</h3>
<p>
After a twenty-minute panic-faff about the diesel level and a very windy road,
we found the start of the path to the cave guarded by a small family of pigs.
None of the maps or instructions were of any use in finding the cave entrance,
which we only found after three hours of scrambling in brambles when Rik washed
his dreads in the river and stumbled across the entrance by accident.<br>
<br>
Once we squeezed through the gap in the locked gate we descended a twenty metre
pitch/climb into the first chamber. Scrambled down and down until we found the
stream-way. Water! We were all wearing wetsuits and the cave was warm, so the
water and the wet duck that followed were most welcome. More small passage, then
the cave really opened out and became much more inviting. Beautiful crystals
glittering from flowstone rocks, pretty things hanging from the ceiling and
mysterious still pools shaped like enormous gills. We got almost as far as the
confluenza and then turned back.<br>
<br>
We got a shock to see that we were an hour past call-out when we got out.&nbsp;
Drive out was a bit stressful because the windscreen steamed up &amp; we went
over a large rock that wasn't there on the way down, but we got home with petrol
to spare.<br>
<br>
We left the ropes in the cave at the pushing front.</p>
<p align=right>Tom Brown</p>
  
