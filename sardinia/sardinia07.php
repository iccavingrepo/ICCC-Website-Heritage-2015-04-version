<h2>Sat:</h2>
<p>
<a href="/caving/photo_archive/tours/2007%20-%20sardinia/jarvist%20frost%20-%20tom%20sleeping%20in%20stansted.html">
<img align=left src="/caving/photo_archive/tours/2007%20-%20sardinia/jarvist%20frost%20-%20tom%20sleeping%20in%20stansted--thumb.jpg">
