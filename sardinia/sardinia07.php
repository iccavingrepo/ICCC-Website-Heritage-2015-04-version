No sleep in Stansted, 5am flight. Arrive in cold blustery Sardinia &amp; fight
  our way through passport control. Were it not for the Cacti, I wouldn't
  believe we were in the Mediterranean, rather some Prisoner-esque escapade in
  Port Merrion. The surrealism continues at the car hire place, where they tell
  us that due to a shortage of what we ordered, we get a free triple-upgrade
  to... a Mercedes E270! What? I'm 23? I wouldn't hire a Merc to me. Let alone
  in a country where they all drive on the wrong side of the road.</p>
<p>
<a href="/caving/photo_archive/tours/2007%20-%20sardinia/jana%20carga%20-%20IMG_9788.html">
<img align=right src="/caving/photo_archive/tours/2007%20-%20sardinia/jana%20carga%20-%20IMG_9788--thumb.jpg">
</a>
  Find the cars, drag the overstuffed baggage along. Manage to start it
  eventually. But some-one's nicked the hand brake, and put in an extra pedal
  'down below'. After a few stalls, difficulty finding reverse, a few 3-point
  turns and appalling roads we managed to find somewhere to buy bread and
  espresso.</p>
  
<p>
  Made it somehow to the villa (3hr drive), then went for a Razz to the sea in
  the Merc (Orosei) and gazed at the Med, before checking out a couple of routes
  into Cala Gonone.</p>

<p>
<a href="/caving/photo_archive/tours/2007%20-%20sardinia/jana%20carga%20-%20IMG_9801.html">
<img align=left src="/caving/photo_archive/tours/2007%20-%20sardinia/jana%20carga%20-%20IMG_9801--thumb.jpg">
</a>
  After fighting up past a herd of goats getting fed by a rather cheeky shepherd
  on one of the hair-pin bends on the concrete single-track road we reached the
  summit and parked in the first lay by. Beautiful ridge walk - amazing sculpted
  limestone amphitheatre, found a sprig of wild rosemary for our supper.</p>
<p>
  Back at the Villa, team Keen cooked spaghetti while the rest of us slumbered;
  all ate then slept for 14 hours or so.
</p>
<p align=right>Jarvist Frost</p>


