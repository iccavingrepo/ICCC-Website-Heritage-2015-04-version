Here, though, we got bogged down i nthe route finding and further progress was inevitable punctuated by the usual chattering, hesitant movement and, of course, getting stuck in the squeezes which chip away at one's advenurous resolve. After walking in circles around 'La Confluenza', a maze of water passage and streamway just after the Alta Loma, we finally joined 'The White Nile'. This is a long streamyway which connects to the main body of the cave, including the enormous 'Lilliput' and 'Disneyland' chambers.</p>
<p>

Halfway down the White Nile we reached the '9m Cascade', a fairly small pitch at a waterfall that we had intended to descend with our 20m rope and standard metalwork. Unfortunately a quick survey of the pitch revealed that it would be preferable to return the next day with a bolting hammer and longer rope. We dumped the bags containing our wetsuits before heading out for an early night, itching to get back to the White Nile...
</p>
<p align=right>Richard Venn</p>

