 <a id="tth_sEc12"></a><h2>
12&nbsp;&nbsp;Conclusion</h2>
All aims were completed to different degrees of success.

<ol type="1">
<li> To continue to practice caving to prepare our cavers for the summer expedition.

The group completed 3 caving trips over the week. One of which was to a depth of -300m, most of which was on rope. This magnitude of vertical cave is unavailable in the UK and is very useful preparation for the 1000m deep trips over the summer tour.

<div class="p"><!----></div>
</li>

<li> To introduce members to Via Ferrata; a sport not commonly available in the UK and which uses similar traverse techniques as caving.

Several Via Ferrata routes were completed and every member became exceptionally competent at traversing them.

<div class="p"><!----></div>
</li>

<li> To introduce members to Canyoning; a sport not commonly available in the UK and which uses similar rope techniques to caving.

One canyon was attempted but the team turned round halfway down, deciding that a particular cascade had too much water flowing to safely attempt it. There had been several weeks of heavy rain before the tour so this is likely the cause.

<div class="p"><!----></div>
</li>

<li> To foster a friendly environment to allow members to bond.

Everyone on the trip had a great time (see photos).

<div class="p"><!----></div>
</li>
</ol>

<div class="p"><!----></div>
Considering the team was entirely composed of 1st and 2nd year cavers, none of whom had organised a foreign trip before, the whole experience was surprisingly smooth. They managed to do all three of the sports they intended. It is only canyoning where they would have really benefited from a more experienced guide to take them, and this is something to bear in mind for future trips. One of the more useful legacies of the trip, that can be made use of in the future, is the contacts the club now has in Spain.

